                <ul>
                    <br>
                    <li>
                        fiziki şəxslər şərti maliyyə vahidinin on mislindən on beş mislinədək miqdarda,
                        vəzifəli şəxslər şərti maliyyə vahidinin otuz mislindən əlli mislinədək miqdarda,
                        hüquqi şəxslər şərti maliyyə vahidinin yüz iyirmi mislindən yüz altmış mislinədək
                        miqdarda cərimə edilir.

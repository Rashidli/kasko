                razılıq (lisenziya) tələb olunduğu hallarda belə razılıq olmadan sahibkarlıq fəaliyyəti ilə
                məşğul olmağa, yaxud lisenziyada nəzərdə tutulmuş şərtlərin pozulması ilə və ya xüsusi

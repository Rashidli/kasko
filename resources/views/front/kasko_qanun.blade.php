            <h2>Fiziki şəxslərə təqdim edilən avtomobil nəqliyyatı vasitələrinin <br>
                tam sığortası üzrə sığorta qaydaları
            </h2>
            <br>
            <h2>ÜMUMİ HİSSƏ</h2>
            <br>
            <h2>BÖLMƏ I.</h2>
            <br>
            <h2>Fiziki şəxslərə təqdim edilən avtomobil nəqliyyatı vasitələrinin tam sığortasına dair ümumi şərtlər</h2>
            <br>
            <h3><strong>1. Ümumi müddəalar</strong></h3>
            <br>
            <p>
                1.1. “Fiziki şəxslərə təqdim edilən avtomobil nəqliyyatı vasitələrinin tam sığortası üzrə sığorta qaydaları” (bundan sonra “bu
                Qaydalar”) “Fiziki şəxslərə təqdim edilən müəyyən könüllü sığorta növləri üzrə vahid sığorta qaydaları haqqında” Azərbaycan Respublikası
                Prezidentinin 2014-ci il 22 aprel tarixli 398 nömrəli Sərəncamının 1-ci hissəsinin icrasını təmin etmək məqsədilə hazırlanmışdır və “Sığorta
                fəaliyyəti haqqında” Azərbaycan Respublikasının Qanununun 16.5-ci maddəsinə uyğun olaraq, avtomobil nəqliyyatı vasitələrinin tam
                sığortası üzrə fiziki şəxslərə təqdim edilən könüllü sığorta növünün aparılmasının əsaslandığı, bütün sığortaçılar üçün məcburi olan qayda
                və şərtləri müəyyən edir.
                <br>1.2. Sığorta müqaviləsindən irəli gələn və bu Qaydalarda nəzərdə tutulmayan hallar Azərbaycan Respublikasının Mülki Məcəlləsi ilə
                tənzimlənir.
                <br>1.3. Bu Qaydalar, habelə sığortalı tərəfindən doldurulmuş sığortalanma haqqında ərizə (Əlavə) sığorta müqaviləsinin ayrılmaz
                hissəsi hesab olunur.
            </p>
            <br>
            <h3><strong>2. Əsas anlayışlar</strong></h3>
            <br>
            <p>
                2.0. Bu Qaydalarda aşağıdakı əsas anlayışlardan istifadə olunur:
                <br>2.0.1. <strong>avtomobil-</strong>  yollarda adamların və ya yüklərin daşınması, yaxud adam və ya yük daşıyan nəqliyyat vasitələrinin yedəyə alınması
                üçün istifadə edilən avtomobil nəqliyyatı vasitəsi;
                <br>2.0.2.<strong>sığortaçı –</strong> “Sığorta fəaliyyəti haqqında” Azərbaycan Respublikasının Qanununa əsasən sığorta fəaliyyətini həyata keçirmək üçün müvafiq lisenziyaya malik olan, sığorta müqaviləsində nəzərdə tutulan sığorta hadisəsi baş verdiyi halda sığorta ödənişini vermək öhdəliyi
                daşıyan sığorta müqaviləsinin tərəfi olan sığorta şirkətidir.
                <br>2.0.3. <strong>sığortalı –</strong> sığorta haqqı ödəyərək avtomobili sığorta etdirən sığorta müqaviləsinin tərəfi;
                <br>2.0.4. <strong>sığorta olunan -</strong> sığorta müqaviləsi əsasında avtomobil ilə, o cümlədən avtomobili idarə etməsi ilə bağlı risklərlə əlaqədar əmlak mənafeləri sığortalanan fiziki şəxs;
                <br>2.0.5. <strong>avtomobilin sahibi -</strong> sığortalanan avtomobilin mülkiyyətçisi, yaxud avtomobilə digər qanuni əsasla (icarə hüququ ilə, nəqliyyat
                vasitəsini idarə etmək hüququna dair etibarnamə üzrə və s.) sahiblik edən şəxs;
                <br>2.0.6. <strong>faydalanan şəxs –</strong> sığorta müqaviləsinə uyğun olaraq sığorta ödənişi verilməli olan şəxs;
                <br>2.0.7. <strong>sürücü –</strong> sığortalanmış avtomobili idarə etməsi halında baş verən sığorta hadisələrinə sığorta təminatı verilən fiziki şəxs, bu
                Qaydaların 5.3-cü bəndində nəzərdə tutulmuş halda isə sığorta müqaviləsində sığorta olunan qismində göstərilən şəxslərin siyahısına daxil
                edilən fiziki şəxs.
                <br>2.0.8. <strong>sığorta riski –</strong> sığorta obyekti ilə bağlı itkilərin və ya zərərlərin yaranmasına səbəb olan hadisənin baş verməsi və ya halın
                yaranması ehtimalı;
                <br>2.0.9. <strong>sığorta hadisəsi –</strong> sığorta müqaviləsinə görə sığorta ödənişinin sığortalıya, sığorta olunana və ya digər faydalanan şəxslərə
                ödənilməsi üçün əsas olan, sığorta müqaviləsinin qüvvədə olduğu müddət ərzində baş verən hadisə və ya yaranan hal;
                <br>2.0.10. <strong>sığorta ödənişi –</strong> sığorta hadisəsi baş verdikdə, sığorta müqaviləsinə uyğun olaraq sığortaçı tərəfindən ödənilən maliyyə
                kompensasiyası;
                <br>2.0.11. <strong>sığorta müqaviləsi –</strong> sığortalının müvafiq sığorta haqqı ödəməsi müqabilində avtomobilin məruz qala biləcəyi risklərlə bağlı
                itkilərin, dəyən zərərin əvəzinin və ya razılaşdırılmış pul məbləğinin müəyyən bir hadisənin baş verməsi əsasında ödənilməsinin sığortaçı
                tərəfindən öhdəlik kimi götürülməsi şərtlərinin təsbit edildiyi yazılı razılaşma;
                <br>2.0.12.<strong>sığorta şəhadətnaməsi –</strong> sığorta müqaviləsinin bağlanmasını təsdiq edən və sığortaçı tərəfindən sığortalıya və (və ya) sığorta
                olunana verilən sənəd;
                <br>2.0.13. <strong>sığorta obyekti –</strong> sığortalının, yaxud sığorta olunanın avtomobilə sahibliyi ilə bağlı qanunazidd olmayan əmlak mənafeləri;
                <br>2.0.14. <strong>sığorta predmeti –</strong> bu Qaydaların II Bölməsinə münasibətdə sığortalanan avtomobil, III Bölməsinə münasibətdə həmin
                avtomobilin sahibinin mülki məsuliyyətinin yaranması halı, IV Bölməsinə münasibətdə isə həmin avtomobilin sürücüsü və sərnişinləri;
                <br>2.0.15. <strong>sığorta müddəti –</strong> sığorta müqaviləsinin qüvvədə olduğu müddət;
                <br>2.0.16. <strong>sığorta haqqı –</strong> sığorta etdirilən avtomobillə əlaqədar risklərin qəbul edilməsi və ya bölüşdürülməsi müqabilində sığorta müqaviləsində nəzərdə tutulan qaydada sığortalının sığortaçıya ödəməli olduğu pul məbləği;
                <br>2.0.17. <strong>sığorta məbləği -</strong> sığortalanmış risklər üzrə sığortaçının öhdəliyinin sığorta müqaviləsi ilə müəyyənləşdirilmiş məbləğlə ifadə
                olunan son həddi;
                <br>2.0.18. <strong>sığorta tarifi –</strong> sığorta haqqının hesablanması üçün sığorta məbləğinə tətbiq edilən, faizlə ifadə olunan dərəcə;
                <br>2.0.19. <strong>azadolma məbləği -</strong> sığorta hadisəsi nəticəsində yaranan itkilərin və ya dəyən zərərin sığorta müqaviləsi ilə təminat
                verilməyən və sığortalının üzərində qalan hissəsi;
                <br>2.0.20. <strong>avtomobilin istifadəsi –</strong> avtomobilin yollarda, həmçinin yollara bitişik olan və nəqliyyat vasitələrinin hərəkəti üçün nəzərdə
                tutulmuş həyətlərdə, yaşayış massivlərində, nəqliyyat vasitələri üçün nəzərdə tutulmuş dayanacaqlarda, yanacaqdoldurma məntəqələrində, müəssisə və təşkilatların ərazilərində və digər ərazilərdə istismarı nəzərdə tutulur. Avtomobildə quraşdırılmış, lakin onun
                yol hərəkətində iştirakı ilə birbaşa bağlı olmayan cihazların istismarı nəqliyyat vasitəsinin istifadəsi hesab edilmir;
                <br>2.0.21. <strong>avtomobilin bazar dəyəri –</strong> avtomobilin bazar şərtləri ilə müəyyən edilən qiyməti;
                <br>2.0.22. <strong>sığorta tələbi -</strong> sığorta hadisəsi baş verdikdə sığortalının, sığorta olunanın və ya faydalanan şəxsin sığorta ödənişinin həyata
                keçirilməsi məqsədilə sığortaçıya ünvanladığı müraciət;

            </p>
            <br>
            <h3><strong>3. Sığortanın sinifləri</strong></h3>
            <br>
            <p>
                3.1. Bu Qaydalar fiziki şəxslərin sahibliyində olan avtomobillərin tam sığortası növü üzrə aşağıdakı sığorta siniflərini əhatə edir:
                <br>3.1.1. avtonəqliyyat vasitələrinin sığortası;
                <br>3.1.2. avtonəqliyyat vasitəsi sahiblərinin mülki məsuliyyətinin sığortası;
                <br>3.1.3. fərdi qəza sığortası.
                <br>3.2. Sığortalının seçimi əsasında sığorta müqaviləsində göstərilməklə bu Qaydaların 3.1-ci bəndində nəzərdə tutulan bir, yaxud bir
                neçə sığorta sinfi üzrə bu Qaydaların Xüsusi hissəsininin müvafiq bölmələrində müəyyən edilən şərtlərlə sığorta təminatı verilir.

            </p>
            <br>
            <h3><strong>4. Sığortanın predmeti və sığorta riskləri</strong></h3>
            <br>
            <p>
                4.1. Sığorta predmeti və sığorta riskləri bu Qaydaların Xüsusi Hissəsində hər bir bölmə üzrə ayrılıqda müəyyən edilir.
                <br>4.2. Sığorta müqaviləsində razılaşdırılmaqla bu Qaydaların Xüsusi Hissəsində nəzərdə tutulmayan risklər, o cümlədən digər
                sığorta siniflərinə daxil olan risklər üzrə sığorta təminatı verilə bilər.

            </p>
            <br>
            <h3><strong>5. Sığorta müqaviləsinin tərəfləri və onun aid olduğu digər şəxslər</strong></h3>
            <br>
            <p>
                5.1. <strong>Sığortaçı</strong> sığorta müqaviləsinin tərəfi olan sığorta şirkətidir.
                <br>5.2. İstənilən fəaliyyət qabiliyyətli fiziki şəxs <strong>sığortalı</strong> qismində sığortaçı ilə sığorta müqaviləsi bağlayaraq sahibliyində olan
                avtomobillə bağlı riskləri bu Qaydalara əsasən sığorta etdirə bilər.
                <br>5.3. Sığorta müqaviləsində yalnız <strong>sığorta olunan</strong> qismində göstərilən şəxslərin (sürücülərin) avtomobili idarə etdiyi hallara sığorta
                təminatının verilməsi şərti müəyyən edilərsə, bu Qaydaların 5.4-cü bəndində nəzərdə tutulmuş hal istisna olmaqla, başqa şəxslərin
                avtomobili idarə etməsi ilə bağlı risklər sığortalanmış hesab edilmir.
                <br>5.4. Sığorta müqaviləsində fiziki şəxs olan sığortalı sığorta olunan sürücülərin siyahısında göstərilmədikdə də onun sığorta olunan
                qismində tanınması heç bir halda, o cümlədən müqavilə şərtləri ilə məhdudlaşdırıla bilməz.
                <br>5.5. Bu Qaydaların II Bölməsi üzrə sığorta müqaviləsində <strong>faydalanan şəxs</strong> qismində başqa şəxs nəzərdə tutulmamışdırsa, sığortalı
                faydalanan şəxs sayılır. Bu Qaydaların III Bölməsi üzrə zərərçəkən üçüncü şəxs və (və ya) ona dəymiş zərərin əvəzini ödəmiş, lakin zərərin
                vurulmasında təqsirli olmayan digər şəxs, habelə zərərçəkən üçüncü şəxsin ailə üzvləri faydalanan şəxs sayılır. Bu Qaydaların IV Bölməsi
                üzrə sağlamlığına zərər dəymiş sürücülər və (və ya) sərnişinlər, həmçinin onların ailə üzvləri faydalanan şəxs sayılır.

            </p>
            <br>
            <h3><strong>6. Sığorta müqaviləsinin bağlanması</strong></h3>
            <br>
            <p>
                6.1. Sığorta müqaviləsi sığortalının sığortalanma haqqında ərizəsi əsasında bağlanır. Sığortalanma haqqında ərizə Bu Qaydalara
                Əlavədə nəzərdə tutulmuş ərizə formasının doldurulması yolu ilə tərtib edilir.
                <br>6.2. Sığorta müqaviləsi yazılı olaraq aşağıdakı hər hansı bir formada bağlanılır:
                <br>6.2.1. tərəflərin bu Qaydalar əsasında sığorta müqaviləsi adlanan sənədi tərtib edərək qarşılıqlı imzalaması yolu ilə;
                <br>6.2.2. Sığortalının bu Qaydalarla tanış olmasını və onun şərtləri ilə razılaşmasını yazılı qaydada təsdiq etməsi şərti ilə sığortaçı
                tərəfindən sığortalıya sığorta şəhadətnaməsinin verilməsi yolu ilə.
                <br>6.3. Bu Qaydaların 6.2.2-ci yarımbəndində nəzərdə tutulmuş halda sığorta şəhadətnaməsində sığorta predmetinin sığortalandığı
                risklər konkret olaraq sadalanmalıdır.
                <br>6.4. Sığorta müqaviləsinin bu Qaydaların 6.2-ci bəndində nəzərdə tutulmuş formaları elektron sənəd formasında da bağlana bilər.
                <br>6.5. Sığorta müqaviləsi bağlanarkən sığortaçı sığortalıya hamı tərəfindən asanlıqla başa düşülən üslubda tərtib edilmiş və
                aşağıdakıları əks etdirən yaddaş vərəqi verməlidir:
                <br>6.5.1. sığorta hadisəsi hesab edilə bilən halın baş verdiyi zaman necə hərəkət etməyin lazım olduğunu;
                <br>6.5.2. sığortaçının sığorta ödənişindən imtina etməsinin qanuni əsaslarını.
            </p>
            <br>
            <h3><strong>7. Sığorta müqaviləsinə əlavələr və dəyişikliklərin edilməsi</strong></h3>
            <br>
            <p>
                7.1. Sığorta müqaviləsinin qüvvədə olduğu müddət ərzində ona tərəflərin razılığı ilə sığortalının vəziyyətini bu Qaydaların şərtlərinə
                nisbətən pisləşdirməyən əlavələr və dəyişikliklər edilə bilər. Bu tələbə uyğun olmayan əlavələr və dəyişikliklər etibarsızdır.
                <br>7.2. Sığortaçı sığorta müqaviləsinin qüvvədə olduğu müddətdə sığorta riskinin artmasına səbəb olan dəyişikliklər barədə məlumat
                aldıqda sığorta müqaviləsinin şərtlərinin dəyişdirilməsini, o cümlədən sığorta haqqının artırılmasını tələb edə bilər. Sığortalı bu tələblə
                razılaşmadıqda, sığortaçı sığorta müqaviləsinə vaxtından əvvəl xitam verilməsini tələb edə bilər
            </p>
            <br>
            <h3><strong>8. Sığorta müddəti və sığorta ərazisi</strong></h3>
            <br>
            <p>
                8.1. Sığortanın müddəti sığorta müqaviləsi ilə müəyyən olunur.
                <br>8.2. Sığorta müqaviləsi bir ildən artıq müddətə bağlandıqda sığorta müqaviləsində sığortanın hər ili üçün sığorta məbləği və sığorta
                haqqı ayrılıqda göstərilir.
                <br>8.3. Sığortaçının sığorta müqaviləsi üzrə öhdəlikləri sığorta müqaviləsində göstərilən sığorta müddətinin başlanması tarixindən
                yaranır, bu şərtlə ki, sığorta haqqı və ya onun sığorta müqaviləsi ilə müəyyən olunmuş ilk hissəsi həmin tarixdən etibarən 1 aydan gec
                olmayaraq ödənilmiş olsun.
                <br>8.4. Sığorta haqqının sığorta müqaviləsi ilə müəyyən olunmuş növbəti hissəsi sığorta müqaviləsində həmin hissənin odənilmə tarixi
                kimi müəyyən edilən tarixdən sonrakı 15 gün müddətində ödənilmədikdə, sığorta müqaviləsi həmin müddətin başa çatması ilə qüvvədən
                düşmüş hesab olunur.
                <br>8.5. Sığorta müqaviləsində başqa cür nəzərdə tutulmamışdırsa, sığorta müqaviləsi Azərbaycan Respublikasının ərazisində
                qüvvədədir.
                <br>8.6. Sığorta müqaviləsinin qüvvədə olduğu ərazi tərəflərin yazılı razılığı ilə dəyişdirilə bilər.

            </p>
            <br>
            <h3><strong>9. Sığorta müqaviləsinin tərəflərinin hüquq və vəzifələri</strong></h3>
            <br>
            <p>
                9.1. Sığortalının hüquqları:
                <br>9.1.1. sığorta müqaviləsi bağlanarkən sığortalı sığortaçının müstəqil auditor tərəfindən təsdiqlənmiş illik balansı və fəaliyyətinin illik
                yekunlarına dair maliyyə nəticələri ilə tanış olmaq;
                <br>9.1.2. sığorta şəhadətnaməsi itdiyi halda sığortaçıdan onun dublikatını almaq;
                <br>9.1.3. sığorta müqaviləsinin şərtləri və bu Qaydalar barədə sığortaçıdan izahatlar almaq;
                <br>9.1.4. sığorta müqaviləsinin şərtlərinin dəyişdirilməsi təklifi ilə çıxış etmək;
                <br>9.1.5. qanunla və bu Qaydalarda nəzərdə tutulmuş digər hüquqlardan istifadə etmək;
                <br>9.2. Sığortalının vəzifələri:
                <br>9.2.1. sığortalanma haqqında ərizədə qoyulan suallara doğru cavablar vermək;
                <br>9.2.2. sığorta haqqını sığorta müqaviləsində nəzərdə tutulmuş müddətdə və miqdarda ödəmək;
                <br>9.2.3. sığortaçını sığorta hadisəsi barədə yazılı şəkildə məlumatlandırmaq. Bu vəzifə faydalanan şəxs tərəfindən də yerinə yetirildikdə
                qənaətbəxş hesab edilir;
                <br>9.2.4. sığorta müqaviləsi bağlanarkən sığortalanma haqqında ərizə formasında göstərilən və özünə məlum olan, sığortaçının müqavilədən imtina etmək, yaxud onu məzmunu dəyişdirilmiş şəkildə bağlamaq qərarına təsir göstərə bilən bütün hallar barədə
                sığortaçıya məlumat vermək;
                <br>9.2.5. Sığorta müqaviləsi bağlandıqdan sonra həmin müqavilənin qüvvədə olduğu müddət ərzində sığorta riskinin dəyişməsinə təsir
                göstərə bilən aşağıdakı hallar barədə bildiyi və ya bilməli olduğu tarixdən 5 iş günü müddətində bu barədə sığortaçıya yazılı məlumat
                vermək:
                <br>9.2.5.1. avtomobilin özgəninkiləşdirilməsi, onun icarəyə verilməsi və ya ona olan mülkiyyət hüquqlarının digər formada itirilməsi və ya məhdudlaşması;
                <br>9.2.5.2. nömrəli aqreqatların – mühərrik, şassi və banın dəyişdirilməsi;
                <br>9.2.5.3. avtomobilin dövlət qeydiyyatı haqqında sənədlərinin itməsi, oğurlanması və ya dəyişdirilməsi;
                <br>9.2.5.4. avtomobilin açarlarının və elektron aktivləşdirmə kartının itirilməsi və ya oğurlanması;
                <br>9.2.6. sığorta müqaviləsinin qüvvədə olduğu müddət ərzində avtomobilin bu Qaydaların 5.3-cü bəndində nəzərdə tutulmuş halda
                sığorta müqaviləsində sığorta olunan qismində göstərilməyən şəxslərə avtomobili idarəetmə hüququnun verilməsindən əvvəl qeyd olunan
                hallara görə sığorta təminatının sığorta müqaviləsinə daxil edilməsi üçün sığortaçıya yazılı məlumat vermək;
                <br>9.2.6. qanunla və bu Qaydalarda nəzərdə tutulan digər vəzifələri yerinə yetirmək;
                <br>9.3. Sığortaçının hüquqları:
                <br>9.3.1. sığortalının verdiyi məlumatların düzgünlüyünü yoxlamaq;
                <br>9.3.2. sığortalının sığortalanma haqqında ərizədə qoyulan suallara qəsdən həqiqətə uyğun olmayan cavablar verməsi sığorta müqaviləsinin qüvvəyə mindiyi tarixdən sonra aşkar edildikdə, həmin müqaviləyə vaxtından əvvəl xitam verilməsi tələbi ilə çıxış etmək;
                <br>9.3.5 .qanunla və bu Qaydalarda nəzərdə tutulmuş digər hüquqlardan istifadə etmək;
                <br>9.4. Sığortaçının vəzifələri:
                <br>9.4.1. sığortalıya bu Qaydalar əlavə edilmiş sığorta şəhadətnaməsi vermək;
                <br>9.4.2. sığorta hadisəsi hesab edilə bilən, araşdırılması və ya qeydə alınması tələb olunan hadisələr barədə səlahiyyətli dövlət
                orqanlarına belə hadisələrin baş verməsi faktını və (və ya) səbəbini, habelə nəticələrini təsdiq edən sənədin alınması üçün yazılı sorğu
                göndərmək;
                <br>9.4.3. qanunla və bu Qaydalarda nəzərdə tutulan digər vəzifələri yerinə yetirmək.
                <br>9.5. Aşağıdakı hallarda sığortaçının sığortalı qarşısında öhdəlikləri tam olaraq yerinə yetirilmiş hesab edilir:
                <br>9.5.1. Sığorta müqaviləsi üzrə verilmiş sığorta ödənişlərinin ümumi həcmi ümumi sığorta məbləğinə bərabər olduqda;
                <br>9.5.2. avtomobilin oğurlanması, qaçırılması və ya tam məhv olmasına görə sığorta müqaviləsinə uyğun olaraq sığorta ödənişi
                verildikdə;
                <br>9.5.3. sığorta müqaviləsi üzrə sığorta məbləği ilk sığorta hadisəsi üzrə nəzərdə tutulduğu hallarda, ilk sığorta hadisəsi üzrə sığorta
                ödənişi verildikdə.
            </p>
            <br>
            <h3><strong>10. Təmir emalatxanasının təyin edilməsi barədə müddəa</strong></h3>
            <br>
            <p>
                Bu Qaydaların Xüsusi hissəsinin II və III Bölmələri üzrə zərər dəyən avtomobilin təmir edilməsi üçün təmir emalatxanasını (servis mərkəzi və s.) təyin etməklə bağlı seçim hüququnun sığortaçıya və ya sığorta olunana aid edilməsi sığorta müqaviləsində müəyyən edilir.
            </p>
            <br>
            <h3><strong>11. Sığorta müqaviləsinə vaxtından əvvəl xitam verilməsi</strong></h3>
            <br>
            <p>
                11.1. Sığorta müqaviləsinə aşağıdakı hallarda vaxtından əvvəl xitam verilir:
                <br>11.1.1. sığorta predmeti artıq mövcud olmadıqda;
                <br>11.1.2. bu Qaydaların 11.3-cü bəndində nəzərdə tutulmuş hallar istisna olmaqla, sığortalı fiziki şəxs öldükdə;
                <br>11.1.3. sığorta müqaviləsi üzrə sığortalı olmayan sığorta olunanın öldüyü halda sığortalının onun başqası ilə əvəz olunması təklifinə
                sığortaçı etiraz etdikdə;
                <br>11.1.4. sığorta hadisəsinin baş verməsi ehtimalı olmadıqda və sığorta riskinin mövcudluğu, səbəbi sığorta hadisəsi olmayan hallara
                görə başa çatdıqda;
                <br>11.1.5. sığortaçı sığortalı qarşısında öz öhdəliklərini tam olaraq yerinə yetirdikdə;
                <br>11.1.6. sığortalı sığorta haqqını sığorta müqaviləsində müəyyən edilən qaydada ödəmədikdə;
                <br>11.1.7. sığortalının avtomobilə münasibətdə sığorta marağı artıq mövcud olmadıqda;
                <br>11.1.8. sığortalı və ya sığortaçı sığorta müqaviləsinə vaxtından əvvəl xitam verilməsi tələbi ilə çıxış etdikdə.
                <br>11.2. Sığorta müqaviləsinin qüvvədə olduğu dövrdə sığortalı məhkəmənin qərarı ilə fəaliyyət qabiliyyəti olmayan hesab edilmişdirsə,
                yaxud onun fəaliyyət qabiliyyəti məhkəmənin qərarı ilə məhdudlaşdırılmışdırsa, şəraitdən asılı olaraq aşağıdakılar tətbiq edilir:
                <br>11.2.1. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə müqavilə (və ya sığorta müqaviləsinin avtomobil sahiblərinin mülki məsuliyyətinin sığortasına aid olan bölməsi) məhkəmə qərarının qanuni qüvvəyə mindiyi andan xitam verilmiş hesab edilir. Məhkəmənin müvafiq qərarının qanuni qüvvəyə mindiyi andan əvvəlki tarixdə sığortalanmış avtomobilin idarə edilməsinin qanuni əsaslarla başqa şəxsə
                də həvalə edildiyi və bununla sığortaçının razılaşdığı hallarda isə sığorta müqaviləsi (və ya sığorta müqaviləsinin avtomobil sahiblərinin mülki məsuliyyətinin sığortasına aid olan bölməsi) üzrə sığortalının hüquq və vəzifələri həmin şəxsə keçir;
                <br>11.2.2. Avtomobillərin sığortası üzrə, habelə avtomobillərin sürücüsü və sərnişinlərinin fərdi qəza sığortası üzrə sığortalının hüquq və
                vəzifələrini, sığortaçının razılaşdığı hallarda, onun qəyyumu və ya himayəçisi həyata keçirir.
                <br>11.3. Sığortalı fiziki şəxs öldükdə, aşağıdakı hallarda sığorta müqaviləsinə vaxtından əvvəl xitam verilmir:
                <br>11.3.1. Sığortalı sığorta müqaviləsi bağlayarkən sığortalanmış avtomobili qəbul edəcək hər hansı şəxsi təyin etmiş olduğu, habelə
                sığorta hadisəsi baş verənədək sığortaçının razılığı ilə onu dəyişdirmiş olduğu hallarda sığortalı fiziki şəxs ölərsə, onun həmin müqavilə
                üzrə hüquq və vəzifələri Azərbaycan Respublikasının Mülki Məcəlləsi ilə müəyyən olunmuş vərəsəlik qaydasında sığortalanmış əmlakı
                qəbul edən şəxsə keçir;
                <br>11.3.2. Sığorta müqaviləsində başqa şərtlər müəyyən edilməmişdirsə, sığortalının hüquq və vəzifələri sığorta predmeti olan miras
                əmlakın yeni mülkiyyətçisinə, sahibinə və ya istifadəçisinə həm onun özünün, həm də sığortaçının müqavilədə rəsmiləşdirilən razılığı ilə
                keçir.

            </p>
            <br>
            <h3><strong>12. Sığorta müqaviləsinə vaxtından əvvəl xitam vermə ilə bağlı xəbərdar etmə</strong></h3>
            <br>
            <p>
                12.1. Sığorta müqaviləsinə xitam verilməsi üçün əsas olan şərait yarandıqda, bu Qaydaların 12.2-ci bəndi nəzərə alınmaqla, müqaviləyə xitam verilməsində maraqlı olan tərəf dərhal digər tərəfi bu barədə xəbərdar etməlidir.
                <br>12.2. Sığorta müqaviləsinə bu Qaydaların 11.1.8-ci yarımbəndinə uyğun olaraq, sığortalının və ya sığortaçının tələbi ilə vaxtından
                əvvəl xitam verilərkən bu barədə bir tərəf digərinə ən azı 30 gün əvvəl (sığorta müqaviləsi 3 aydan az müddətə bağlanmış olduqda isə 5 iş
                günü əvvəl) yazılı bildiriş göndərməlidir.
                <br>12.3. İpotekada olan avtomobillə bağlı sığorta müqaviləsinə sığortalının tələbi ilə vaxtından əvvəl xitam vermə yalnız ipoteka
                saxlayanın razılığı ilə həyata keçirilir.

            </p>
            <br>
            <h3><strong>13. Sığorta müqaviləsinə vaxtından əvvəl xitam verməninnəticələri</strong></h3>
            <br>
            <p>
                13.1. Sığorta müqaviləsinə vaxtından əvvəl xitam verilməsi halında sığorta haqlarının müvafiq hissəsinin qaytarılması ilə bağlı münasibətlər aşağıdakı qaydada həll edilir.
                <br>13.1.1. Sığorta müqaviləsinə (qrup halında sığorta zamanı həm də müqaviləyə hər hansı avtomobilə münasibətdə) sığortalının
                tələbi ilə vaxtından əvvəl xitam verildikdə sığortaçı müqavilənin (qrup halında sığorta zamanı müqavilənin hər hansı avtomobilə münasibətdə) qurtarmamış müddəti üçün sığorta haqlarını ona qaytarır. Bu halda sığortaçı sığorta haqqının qaytarılan hissəsindən sığorta müqaviləsi üzrə (qrup halında sığorta zamanı isə müqavilənin hər hansı avtomobillə bağlı sığorta haqqına mütənasib olan) işlərin
                aparılması xərclərinin həmin müqavilənin qurtarmamış müddətinə mütənasib hissəsini çıxır. Əgər sığortalının sığorta müqaviləsinə xitam
                verilməsi ilə bağlı tələbi sığortaçının sığorta müqaviləsi üzrə vəzifələrini yerinə yetirməməsi ilə bağlıdırsa, sığortaçı sığorta haqlarını (qrup
                halında sığorta zamanı həm də müqavilənin hər hansı avtomobil üzrə ödənilmiş sığorta haqlarını) bütünlüklə sığortalıya qaytarır.
                <br>13.1.2. Sığorta müqaviləsinə (qrup halında sığorta zamanı həm də müqaviləyə hər hansı avtomobilə münasibətdə) sığortaçının
                tələbi ilə vaxtından əvvəl xitam verildikdə o, sığorta haqlarını (qrup halında sığorta zamanı həm də müqavilənin hər hansı avtomobil üzrə
                ödənilmiş sığorta haqlarını) bütünlüklə sığortalıya qaytarır. Əgər bu tələb sığortalının sığorta müqaviləsi üzrə vəzifələrini yerinə
                yetirməməsi ilə bağlıdırsa, sığortaçı müqavilənin qurtarmamış müddəti üçün sığorta haqlarını (qrup halında sığorta zamanı həm də müqavilənin hər hansı avtomobil üzrə ödənilmiş sığorta haqlarını) qaytarır Bu halda sığortaçı sığorta haqqının qaytarılan hissəsindən
                sığorta müqaviləsi üzrə (qrup halında sığorta zamanı isə müqavilənin hər hansı avtomobillə bağlı sığorta haqqına mütənasib olan) işlərin
                aparılması xərclərinin müqavilənin qurtarmamış müddətinə mütənasib hissəsini çıxır.
                <br>13.1.3. Sığorta müqaviləsinə (qrup halında sığorta zamanı, həm də müqaviləyə hər hansı bir avtomobilə münasibətdə) vaxtından
                əvvəl xitam verildiyi halda, əgər xitam verilmə anınadək sığortaçı tərəfindən sığortalıya ödənilmiş sığorta haqqına (qrup halında sığorta
                zamanı, həm də müqavilənin hər hansı bir avtomobil üzrə ödənilmiş sığorta haqlarına) bərabər və ya ondan çox miqdarda sığorta ödənişi
                verilmişdirsə, sığorta haqqı (qrup halında sığorta zamanı, həm də müqavilənin hər hansı bir avtomobil üzrə ödənilmiş sığorta haqları)
                sığortalıya qaytarılmır.
                <br>13.1.4. Sığorta müqaviləsinə vaxtından əvvəl xitam verildiyi halda, əgər xitam verilmə anınadək sığortaçı tərəfindən sığortalıya
                ödənilmiş sığorta haqqından (qrup halında sığorta zamanı, həm də müqavilənin hər hansı bir avtomobil üzrə ödənilmiş sığorta
                haqlarından) az miqdarda sığorta ödənişi verilmişdirsə, həmin sığorta haqqı məbləği ilə sığorta ödənişi məbləği arasındakı fərq miqdarında
                sığorta haqqının sığortalıya qaytarılması müvafiq olaraq bu Qaydaların 13.1.1-ci və 13.1.2-ci yarımbəndlərində nəzərdə tutulmuş qaydada
                həyata keçirilir.
                <br>13.1.5. Sığorta müqaviləsinə bu Qaydaların 11.2-ci bəndində müəyyən edilmiş məhkəmə qərarı əsasında xitam verilmiş hesab
                edildikdə sığortaçı həmin müqavilə üzrə işlərin aparılması xərclərini çıxmaqla müqavilənin (qrup halında sığorta zamanı həm də müqavilənin hər hansı bir avtomobilə münasibətdə) qurtarmamış müddəti üçün sığorta haqlarını, bu Qaydaların 13.1.3-cü və 13.1.4-cü
                yarımbəndlərinin tələbləri nəzərə alınmaqla, sığortalının qanuni nümayəndəsinə qaytarır.
                <br>13.2. Bu Qaydaların 13.1.1-ci, 13.1.2-ci və 13.1.5-ci yarımbəndlərinin məqsədləri üçün işlərin aparılması xərclərinin qaytarılan sığorta
                haqqı məbləğindən çıxılan hissəsi həmin məbləğin 25 faizindən artıq olmayan həddə müəyyən edilir.

            </p>
            <br>
            <h3><strong>14. Sığorta haqqı</strong></h3>
            <br>
            <p>
                14.1. Sığorta haqqı bu Qaydaların Xüsusi hissəsinə müvafiq olaraq tərtib edilən sığorta müqaviləsinin hər bir bölməsi üzrə ayrıayrılıqda müəyyən edilir.
                <br>14.2. Sığorta müqaviləsində sığorta haqqının birdəfəlik və ya hissə-hissə ödənilməsi razılaşdırıla bilər.
                <br>14.3. Sığorta haqqı və ya onun hissəsi vaxtında ödənilmədikdə sığortaçı onun ödənilməsi üçün bu Qaydaların 14.4-cü bəndinin
                tələbini nəzərə alaraq yazılı surətdə 15 günədək müddət müəyyən edə bilər.
                <br>14.4. Hər bir halda sığorta haqqı və ya onun razılaşdırılmış ilk hissəsi sığorta müqaviləsi bağlandığı gündən 1 aydan gec olmayaraq
                ödənilməlidir.
                <br>14.5. Avtomobilin oğurlanması, qaçırılması və ya tam məhvi ilə nəticələnən sığorta hadisəsi baş verdiyi hallarda həmin hadisənin
                baş verdiyi anadək sığorta haqqı tam məbləğdə ödənilməmişdirsə, sığortaçı sığorta haqqının ödənilməmiş hissəsini sığorta ödənişi məbləğindən çıxır. Digər hallarda sığortaçı sığorta ödənişi məbləğindən sığorta ödənişinin verildiyi tarixə sığortalının ona ödəməli olduğu,
                vaxtı çatmış və ya gecikdirilmiş sığorta haqqı məbləğini tutmaq hüququna malikdir.
                <br>14.6. Elektron sənəd formasında olan sığorta müqaviləsi üzrə sığorta haqqının ödənilməsi sığortalının bu Qaydalar və sığorta müqaviləsinin şərtləri ilə tanış olmasını, həmin qaydalar və şərtlərlə razılığını, həmçinin sığorta müqaviləsinin bağlanması faktını
                təsdiqləyir.
                <br>14.7. Sığortaçı sığorta haqqının hesablanması üçün tətbiq etdiyi sığorta tariflərini və onların iqtisadi əsaslandırılmasını Azərbaycan
                Respublikası Maliyyə Nazirliyinin Kollegiyasının “27” mart 2014-cü il tarixli Q-08 nömrəli Qərarı ilə təsdiq edilmiş “Aktuar hesablamaların
                aparılmasına dair Təlimat”a uyğun olaraq müəyyən edir.

            </p>
            <br>
            <h3><strong>15. Sığorta məbləği</strong></h3>
            <br>
            <p>
                15.1. Sığorta müqaviləsində sığorta məbləğinin aşağıdakı növləri müəyyən oluna bilər:
                <br>15.1.1. <strong>Ümumi sığorta məbləği –</strong> müəyyən edildiyi hallarda sığortaçının sığorta müqaviləsi üzrə öhdəliyinin həcmi verilmiş sığorta
                ödənişi məbləğində azalır.
                Sığortalı ümumi sığorta məbləğinin sığorta müqaviləsində nəzərdə tutulmuş həddə bərpa edilməsini istədikdə bərpa edilməli olan
                hissəyə müvafiq əlavə sığorta haqqı ödəməlidir.
                <br>15.1.2. <strong>Sığorta hadisəsi üzrə sığorta məbləği-</strong> müəyyən edildiyi hallarda həmin məbləğ sığortaçının hər bir sığorta hadisəsi üzrə
                öhdəliyinin son həddi hesab edilir və sığorta hadisələrinə görə sığorta ödənişlərinin verilməsi nəticəsində azalmır.
                <br>15.1.3. </strong>Bir sığorta hadisəsi üzrə sığorta məbləği-</strong> müəyyən edildiyi hallarda sığortaçının sığorta müddəti ərzində baş vermiş yalnız
                bir hadisə üzrə həmin məbləğ həddində sığorta ödənişi vermək öhdəliyi nəzərdə tutulur.
                <br>15.2. Əgər sığorta müqaviləsində və ya onun müvafiq sığorta sinfinə aid edilən bölməsində sığorta məbləğinin bu Qaydaların 15.1-
                ci bəndində göstərilən hər hansı növünün tətbiq olunması nəzərdə tutulmamışdırsa, sığorta məbləğinin 15.1.1-ci yarımbənddə göstərilmiş
                növü tətbiq edilir
            </p>
            <br>
            <h3><strong>Azadolma məbləği</strong></h3>
            <br>
            <p>
                16.1. Sığorta müqaviləsində aşağıdakı növlərdə azadolma məbləğləri müəyyən edilə bilər:
                <br>16.1.1. <strong>Şərtli azadolma məbləği –</strong> bu növ azadolma məbləği nəzərdə tutulduqda, sığorta hadisəsi nəticəsində zərər məbləği,
                avtomobilin qismən sığortası halında isə zərər məbləğinin sığorta ödənişinin həyata keçirilməsi üçün tətbiq edilən hissəsi azadolma məbləğindən çox olduqda və ya sığorta müqaviləsində razılaşdırılmış hər hansı digər halın baş verməsi şərtilə azadolma məbləği tətbiq
                olunmur.
                <br>16.1.2. <strong>Şərtsiz azadolma məbləği -</strong> bu növ azadolma məbləği nəzərdə tutulduqda azadolma məbləği zərər məbləğindən,
                avtomobilin qismən sığortası halında isə zərər məbləğinin sığorta ödənişinin həyata keçirilməsi üçün tətbiq edilən hissəsindən çıxılır.
                <br>16.2. Sığorta müqaviləsində nəzərdə tutulmuş azadolma məbləğinin şərtli və ya şərtsiz olması müəyyənləşdirilməmişdirsə, həmin müqavilə üzrə şərtsiz azadolma məbləği tətbiq olunur.
                <br>16.3. Azadolma məbləği pul məbləği ilə ifadə olunur.
                <br>16.4. Bu Qaydaların 39.2-ci bəndində müəyyən edilmiş hal istisna olmaqla, sığorta müqaviləsində müxtəlif hallar üçün müxtəlif
                azadolma məbləğləri müəyyən oluna bilər.
                <br>16.5. Avtomobilə qismən zərər dəyməsi və avtomobilin tam məhv olması hallarında tətbiq olunan azadolma məbləğləri eyni məbləğdə müəyyən edilməlidir.

            </p>
            <br>
            <h3><strong>17. Sığorta təminatında məhdudiyyətlər</strong></h3>
            <br>
            <p>
                17.1. Sığorta müqaviləsində başqa cür nəzərdə tutulmursa, aşağıdakılara münasibətdə sığorta təminatı verilmir:
                <br>17.1.1. aşağıda göstərilən hallarda dəyən zərər:
                <br>17.1.1.1. avtomobilin texniki təyinatı üzrə istifadə edilməməsi;
                <br>17.1.1.2. təmir və ya xidmət olunma məqsədilə texniki xidmət müəssisəsinin və ya yuyulmaq üçün avtoyuyucunun sərəncamında
                olduğu hallar istisna olmaqla, avtomobilin bu Qaydalara və sığorta müqaviləsinə görə sürücü hesab edilməyən şəxs tərəfindən idarə
                olunması;
                <br>17.1.1.3. avtomobili alkoqolun, narkotik vasitələrin, psixotrop, toksik və güclü təsir göstərən digər maddələrin təsirindən sərxoş
                vəziyyətdə olan və ya onu idarə etməyə hüququ olmayan şəxsin idarə etməsi;
                <br>17.1.1.4. hərbi əməliyyatlar, terrorçuluq, kütləvi iğtişaşlar, tətillər, vətəndaş müharibəsi;
                <br>17.1.1.5. nüvə partlayışı, radioaktiv çirklənmə, nüvə enerjisinin istənilən formada təsiri;
                <br>17.1.1.6. avtomobilin təlim məqsədilə, yaxud rəsmi və qeyri-rəsmi yarışlarda, sürət müsabiqələrində, sürət sınaqlarında, avtomobil
                rallilərində, davamlılıq sınaqlarında istifadə edilməsi;
                <br>17.1.1.7. dövlət orqanları tərəfindən və ya onların göstərişi ilə avtomobilin daşınması (sığorta hadisəsi nəticəsində idarə olunmaq
                imkanlarını itirmiş atomobilin daşınması halları istisna olmaqla), saxlanması və ya məhv edilməsi;
                <br>17.1.1.8. Azərbaycan Respublikasının Mülki Məcəlləsində, Azərbaycan Respublikasının İnzibati Xətalar Məcəlləsində və Azərbaycan
                Respublikasının Cinayət Məcəlləsində nəzərdə tutulmuş məsuliyyəti istisna edən hallardan başqa, sığortalının və ya sığorta olunanın
                sığorta hadisəsinin baş verməsinə yönələn qəsdən etdiyi hərəkəti və ya hərəkətsizliyi;
                <br>17.1.1.9. avtomobilin sığorta ərazisindən kənarda istismar olunması.
                <br>17.1.1.10. avtomobilin cinayət məqsədilə istifadə edilməsi;
                <br>17.1.1.11. avtomobilin sığortalının ailə üzvləri, işçiləri və ya ona xidmət edən şəxslər tərəfindən qaçırılması, talanması və ya bunlara
                cəhd edilməsi;
                <br>17.1.1.12. avtomobilin təyyarələrin enib-qalxdığı təyyarə limanı, aerodrom və ya hərbi bazanın ərazisində istifadə edilməsi;
                <br>17.1.1.13. avtomobilin yüklənməsi və boşaldılması nəticəsində dəyən zərərlər;
                <br>17.1.2. mənəvi zərər;
                <br>17.1.3. əldən çıxan gəlir, gəlir itkisi, işin dayanması ilə bağlı və ya digər dolayı və kommersiya itkiləri, zərərləri və xərcləri;
                <br>17.1.4. Bu Qaydaların Xüsusi hissəsində nəzərdə tutulmuş digər məhdudiyyətlər.
                <br>17.2. Bu Qaydaların 17.1.1.1-ci yarımbəndində “avtomobilin texniki təyinatı üzrə istifadəsi” dedikdə, onun sərnişin daşınması və ya
                yük daşınması, yaxud da adam və ya yük daşıyan nəqliyyat vasitələrinin yedəyə alınması üçün istifadə olunması başa düşülür.
                <br>17.3. sığorta hadisəsi onun baş verməsinə yönələn, sığorta müqaviləsi üzrə sığortalı və (və ya) sığorta olunan hesab edilməyən
                faydalanan şəxsin Azərbaycan Respublikasının Mülki Məcəlləsində, Azərbaycan Respublikasının İnzibati Xətalar Məcəlləsində və
                Azərbaycan Respublikasının Cinayət Məcəlləsində nəzərdə tutulmuş məsuliyyəti istisna edən hallardan başqa qəsdən etdiyi hərəkəti və ya
                hərəkətsizliyi nəticəsində baş verdikdə, həmin faydalanan şəxs sığorta ödənişini almaq hüququndan məhrum olur.
                <br>17.4. Sığorta müqaviləsində bu Qaydalarda müəyyən edilmiş məhdudiyyətlərdən başqa digər məhdudiyyətlər nəzərdə tutula
                bilməz.
                <br>17.5. Bu Qaydalarda müəyyən edilmiş hər hansı məhdudiyyət və ya məhdudiyyətlər tərəflərin razılığı ilə sığorta müqaviləsi üzrə
                sığorta təminatına daxil edilə bilər.

            </p>
            <br>
            <h3><strong>18. Sığorta hadisəsi barədə məlumatlandırma</strong></h3>
            <br>
            <p>
                18.1. Sığorta hadisəsinin baş verməsi barədə sığortalı və ya sığorta olunan şəxs, yaxud faydalanan şəxs hadisədən xəbər tutduqdan mümkün olan ən qısa müddət ərzində sığortaçıya və ya onun nümayəndəsinə, eyni zamanda həmin hadisə barədə məlumatlandırılmalı
                olan səlahiyyətli dövlət orqanlarına hər hansı vasitə ilə xəbər verməlidir.
                <br>18.2. Sığorta hadisəsinin baş verməsi barədə sığortaçıya məlumat vermiş sığortalı və ya sığorta olunan, həmçinin faydalanan şəxs
                bu Qaydaların 18.1-ci bəndində nəzərdə tutulmuş səlahiyyətli dövlət orqanlarına həmin hadisə barədə xəbər verməmişdirsə, sığortaçı qeyd
                olunan orqanları bu hadisə barədə dərhal məlumatlandırmalıdır.
                <br>18.3. Bu Qaydaların 18.1-ci bəndində üzərinə məlumatlandırma vəzifəsi qoyulan şəxslərdən başqa digər şəxslər, o cümlədən
                səlahiyyətli dövlət orqanları və müvafiq hallarda zərərçəkən tərəfindən də sığorta hadisəsinin baş verməsi haqqında hər hansı vasitə ilə
                sığortaçıya xəbər verilməsi sığorta hadisəsi barədə məlumatlandırılma hesab edilir.

            </p>
            <br>
            <h3><strong>19. Zərərin qiymətləndirilməsi</strong></h3>
            <br>
            <p>
                19.1. Sığortaçı dəymiş zərəri müxtəlif üsullarla, o cümlədən zərər dəymiş avtomobilin, yaxud digər predmetin və ya hadisə yerinin
                fiziki və texniki xüsusiyyətlərini müxtəlif vasitələrlə təyin etməklə, sxem və cizgilər tərtib etməklə, foto və video çəkilişlər aparmaqla,
                bilavasitə özü və ya “Sığorta fəaliyyəti haqqında” Azərbaycan Respublikası Qanununun 10.10-cu maddəsinin tələbini nəzərə almaqla
                nümayəndəsi kimi təyin etdiyi sığorta sahəsində yardımçı fəaliyyət göstərən müvafiq şəxs vasitəsilə qiymətləndirir.
                <br>19.2. Sığorta hadisəsi nəticəsində dəyən zərərin sığortaçı tərəfindən qiymətləndirilməsi, habelə miqdarı barədə tərəflər arasında
                razılıq əldə edilmədikdə, zərərin miqdarının qiymətləndirilməsi “Sığorta fəaliyyəti haqqında” Azərbaycan Respublikası Qanununun 10.10-
                cu maddəsinin tələbi nəzərə alınmaqla təyin olunan müstəqil ekspert tərəfindən aparılır.
                <br>19.3. Zərəri qiymətləndirən şəxs sığorta hadisəsinin baş verməsi faktını təsdiq edən və (və ya) sığorta ödənişinin həcminin müəyyənləşdirilməsi üçün lazım olan sənədləri və məlumatları Qaydaların 19.4-cü bəndi nəzərə alınmaqla, sığortalıdan tələb etmək
                hüququna malikdir.
                <br>19.4. Sığorta hadisəsi hesab edilə bilən hadisələr barədə səlahiyyətli dövlət orqanlarına belə hadisələrin baş verməsi faktını və (və ya)
                səbəbini, habelə nəticələrini təsdiq edən sənədin bu Qaydalara əsasən tələb olunduğu hallarda alınması üçün yazılı sorğu vermək
                sığortaçının və ya onun nümayəndəsinin vəzifəsidir.
                <br>19.5. Zərər məbləğinin sığortaçı tərəfindən və ya onun razılığı ilə bu Qaydaların 19.6-cı bəndində nəzərdə tutulmuş müddətdə
                qiymətləndirilməsinin başladığı anadək sığortalı və ya sığorta olunan aşağıdakı hallar istisna olmaqla, zərər dəymiş əmlakı sığorta
                hadisəsindən dərhal sonrakı vəziyyətində saxlamadıqda və ya dəymiş zərərin müvafiq qaydada qiymətləndirilməsi üçün şərait
                yaratmadıqda zərərin sığortaçı tərəfindən müvafiq şəkildə qiymətləndirilməsinin nəticələrini qəbul etməlidir:
                <br>19.5.1. zərərin qarşısının alınması və ya həcminin azaldılması, sığorta hadisəsinin nəticələrinin aradan qaldırılması, yaxud digər
                fəsadlara səbəb olmaması, habelə zərər dəymiş əmlakın sonrakı itkiləri üçün təhlükə ehtimalına səbəb ola bilən nəzarətsiz halda
                qalmaması, başqa şəxslərin hərəkətinə və ya fəaliyyətinə mane olmaması üçün zəruri tədbirlərin görülməsi, o cümlədən bu məqsədlərlə
                əmlakın hadisə yerindən kənarlaşdırılması zamanı zərər dəymiş əmlakın hadisədən dərhal sonrakı vəziyyətdə saxlanması mümkün
                olmadıqda;
                <br>19.5.2. zərər dəymiş əmlakın sığorta hadisəsindən dərhal sonrakı vəziyyətdə saxlanmamasına sığortaçının yazılı razılığı olduqda.
                <br>19.6. Sığortaçı sığorta hadisəsinin baş verməsi barədə bu Qaydalarda nəzərdə tutulmuş qaydada məlumatlandırıldıqdan 5 iş günü müddətində onun nümayəndəsi sığortalanmış avtomobilə və ya zərərçəkən üçüncü şəxsin əmlakına dəymiş zərərin məbləğinin
                qiymətləndirilməsini təşkil etmədikdə sığortalı və sığorta olunan, həmçinin zərər çəkmiş üçüncü şəxs təyin etdiyi müstəqil ekspert və ya mütəxəssis vasitəsilə əmlaka dəymiş real zərərin məbləğinin müəyyənləşdirilməsinə və aradan qaldırılmasına başlaya bilər.
                <br>19.7. Əmlaka dəymiş zərərin vaxtında qiymətləndirilməməsi aşağıdakı səbəblərdən baş verməmişdirsə, sığortalı, sığorta olunan və ya
                zərər çəkmiş üçüncü şəxs tərəfindən bu Qaydaların 19.6-cı bəndində nəzərdə tutulmuş qaydada həyata keçirilmiş qiymətləndirmənin
                nəticələrini sığortaçı qəbul etməlidir:
                <br>19.7.1. sığortalının, sığorta olunanın və ya zərər çəkmiş üçüncü şəxsin zərərin qiymətləndirilməsi üçün şərait yaratmaması;
                <br>19.7.2. səlahiyyətli dövlət orqanının qərarı nəticəsində əmlakın qiymətləndirilməsinin mümkün olmaması.

            </p>
            <br>
            <h3><strong>20. Sığorta ödənişinin verilməsinin əsasları</strong></h3>
            <br>
            <p>
                20.1. Sığorta ödənişi aşağıdakı sənədlərin hər birinin mövcud olduğu halda həyata keçirilir:
                <br>20.1.1. Sığorta hadisəsi baş verdikdə, sığortalının, sığorta olunanın və ya digər faydalanan şəxsin sığortaçıya ünvanladığı, sığorta
                hadisəsinin təfərrüatlarını və nəticəsini əks etdirən sığorta tələbi;
                <br>20.1.2. sığorta hadisəsi hesab edilə bilən hadisə ilə bağlı bu Qaydaların 18.1-ci bəndinə uyğun olaraq hər hansı dövlət orqanına məlumat verilməlidirsə, bu Qaydaların 21-ci bəndi nəzərə alınmaqla həmin orqanın hadisə barədə təqdim etdiyi müvafiq sənəd.
                Avtomobilin oğurlanması və ya qaçırılması halında bu sənəd müvafiq hadisə ilə bağlı açılan cinayət işinin nəticələri barədə Azərbaycan
                Respublikası Daxili İşlər Nazirliyinin səlahiyyətli orqanının (vəzifəli şəxsinin) təqdim etdiyi arayış hesab edilir;
                <br>20.1.3. zərər məbləğini təsdiqləyən sənəd;
                <br>20.1.4. sığorta hadisəsi nəticəsində üçüncü şəxsin və ya sığorta olunanın sağlamlığına dəyən zərər üzrə xəsarətin dərəcəsi haqqında
                həkim arayışı, əlillik halında isə əlilliyin qrupunu, səbəbini və təyin edilməsi tarixini əks etdirən Tibbi-Sosial Ekspert Komissiyasının
                arayışının surəti;
                <br>20.1.5. sığorta hadisəsi nəticəsində üçüncü şəxs və ya sığorta olunan öldükdə, ölümün səbəbi haqqında tibbi arayış və ya müvafiq
                hallarda tibbi ekspertiza rəyi, ölüm haqqında şəhadətnamənin notariat qaydasında və ya "İnzibati icraat haqqında" Azərbaycan
                Respublikası Qanununun 9-cu maddəsində müəyyən edilmiş qaydada təsdiq olunmuş surəti, vərəsənin (hüquq varisinin) vərəsəlik
                hüququnu təsdiq edən sənəd və onun şəxsiyyət vəsiqəsinin surəti;
                <br>20.1.6. sığortalı və ya sığorta olunan tərəfindən əmlaka vurulan zərərin əvəzi ödənilmiş olduqda və ya sağlamlığa vurulan zərərlə
                əlaqədar hər hansı ödəmə həyata keçirildikdə, belə ödəməni təsdiq edən sənədin notariat qaydasında və ya "İnzibati icraat haqqında"
                Azərbaycan Respublikası Qanununun 9-cu maddəsində müəyyən edilmiş qaydada təsdiq edilmiş surəti;
                <br>20.1.7. faydalanan şəxs fiziki şəxs olduqda onun şəxsiyyət vəsiqəsinin surəti, hüquqi şəxs olduqda isə onun sığorta ödənişini almaq
                üçün təyin edilən nümayəndəsinə verilmiş müvafiq etibarnamə;
                <br>20.1.8. sığorta olunanın sürücülük vəsiqəsinin surəti, həmçinin sığorta olunan avtomobilin mülkiyyətçisi olmadıqda müvafiq
                etibarnamənin surəti;
                <br>20.1.9. avtomobilin qeydiyyat şəhadətnaməsinin surəti, avtomobilin oğurlandığı və ya qaçırıldığı halda isə qeydiyyat
                şəhadətnaməsinin əsli. əgər avtomobilin qeydiyyat şəhadətnaməsi oğurlanmış və ya qaçırılmış avtomobilin içərisində qalmışdırsa, onun
                Azərbaycan Respublikası Daxili İşlər Nazirliyinin müvafiq orqanı tərəfindən verilən dublikatı, yaxud cinayət işinə əlavə edilmişdirsə, bu faktı
                təsdiq edən arayış.

            </p>
            <br>
            <h3><strong>21. Hadisə ilə bağlı təsdiqedici sənədin tələb olunmaması</strong></h3>
            <br>
            <p>
                21.1. Aşağıdakı şərtlərin hər birinin eyni zamanda mövcud olduğu hallarda, habelə hadisə iştirakçılarının tərtib etdiyi və həmin
                şərtlərin mövcudluğunu təsdiq edən protokolu sığortaçıya təqdim etmələri şərtilə sığortaçı sığorta hadisəsi hesab edilə bilən hadisə ilə
                bağlı öhdəliklərinin icrası üçün bu Qaydaların 20.1.2-ci yarımbəndində nəzərdə tutulmuş sənədi tələb etmir:
                <br>21.1.1. hadisənin baş verməsində təqsirli olan şəxsin müəyyən edilməsi ilə bağlı sığortaçı, sığortalı və (və ya) üçüncü şəxs arasında mübahisə olmadıqda;
                <br>21.1.2. hadisə nəticəsində sağlamlığa zərər dəymədikdə;
                <br>21.1.3. hadisə nəticəsində dəymiş zərərin həcminin 1000 manatdan çox olmaması hadisə iştirakçıları və sığortaçı tərəfindən
                ağlabatan şəkildə ehtimal edildikdə.
                <br>21.2. Bu Qaydaların 21.1-ci bəndində nəzərdə tutulmuş protokol “İcbari sığortalar haqqında” Azərbaycan Respublikasının Qanununun
                58.5-ci maddəsinə uyğun olaraq, Azərbaycan Respublikası Maliyyə Nazirliyinin 2011-ci il 06 dekabr tarixli Q-01 nömrəli Qərarı ilə təsdiq
                edilmiş “Avtonəqliyyat vasitəsi sahiblərinin mülki məsuliyyətinin icbari sığortası üzrə yol-nəqliyyat hadisəsi iştirakçılarının tərtib etdiyi
                protokolun forması və tərtib edilməsi Qaydası”nda müəyyən edilmiş formaya uyğun tərtib edilir.
            </p>
            <br>
            <h3><strong>22. Sığorta ödənişinin verilməsi və ya ödənişdən imtina edilməsi müddəti</strong></h3>
            <br>
            <p>
                Bu Qaydaların 20.1-ci bəndində göstərilən sənədlərdən sonuncunun sığortaçıya təqdim edildiyi tarixdən 7 iş günündən gec olmayaraq
                sığortaçı sığorta ödənişini faydalanan şəxsə onun istəyinə uyğun olaraq nağd və ya bank hesabına köçürmə yolu ilə ödəməli, müvafiq
                hallarda zərərin qiymətləndirilməsi, əmlakın təmiri, bərpası və ya yenisi ilə əvəz edilməsi üçün görülən tədbir barədə sığortalıya yazılı məlumat verməli, yaxud sığortalıya, sığorta olunana və ya faydalanan şəxsə sığorta ödənişinin verilməsindən imtina haqqında yazılı şəkildə
                əsaslandırılmış bildiriş təqdim etməlidir
            </p>
            <br>
            <h3><strong>23. Sığorta ödənişinin verilməsindən imtinanın əsasları</strong></h3>
            <br>
            <p>
                23.1. Sığortaçı sığorta ödənişinin verilməsindən aşağıdakı hallarda imtina edir:
                <br>23.1.1. Zərər bu Qaydalarda nəzərdə tutulmuş sığorta təminatı verilməyən hallarla bağlı olduqda;
                <br>23.1.2. Bu Qaydaların 18.1-ci bəndinin tələblərinə əməl edilməməsi nəticəsində sığortaçı hadisənin sığorta hadisəsi olub-olmamasını müəyyənləşdirmək imkanından məhrum olduqda;
                <br>23.1.3. aşağıdakı hallar istisna olmaqla, sığortalı, sığorta olunan və ya üçüncü şəxs zərər dəymiş avtomobili və ya digər əmlakı sığorta
                hadisəsindən dərhal sonrakı vəziyyətdə sığortaçıya və ya onun səlahiyyətli nümayəndəsinə təqdim etməməsi nəticəsində sığortaçını zərərin
                həcmini müəyyənləşdirmək imkanından tam və ya qismən məhrum etdikdə:
                <br>23.1.3.1. zərərin qarşısının alınması və ya həcminin azaldılması, sığorta hadisəsinin nəticələrinin aradan qaldırılması, yaxud digər
                fəsadlara səbəb olmaması, habelə zərər dəymiş əmlakın sonrakı itkilər üçün təhlükə ehtimalına səbəb ola bilən nəzarətsiz halda qalmaması,
                başqa şəxslərin hərəkətinə və ya fəaliyyətinə mane olmaması üçün zəruri tədbirlərin görülməsi, o cümlədən bu məqsədlərlə əmlakın hadisə
                yerindən kənarlaşdırılması zamanı zərər dəymiş əmlakın hadisədən dərhal sonrakı vəziyyətdə saxlanması mümkün olmadıqda;
                <br>23.1.3.2. sığortaçı sığorta hadisəsi barədə bu Qaydalarda nəzərdə tutulmuş qaydada məlumatlandırıldıqdan 5 iş günü müddətində
                onun nümayəndəsi zərər dəymiş əmlaka baxış keçirmədikdə;
                <br>23.1.3.3. digər hallarda zərər dəymiş əmlakın sığorta hadisəsindən dərhal sonrakı vəziyyətdə saxlanmamasına sığortaçının yazılı
                razılığı olduqda;
                <br>23.1.4. sığortalı sığortalanmış avtomobilə dəyən zərərin qarşısını almaq və ya həcmini azaltmaq üçün zəruri və mümkün tədbirləri
                görmək iqtidarında olduğu halda, həmin tədbirləri qəsdən görmədikdə; bu zaman sığorta ödənişindən o həcmdə imtina edilə bilər ki,
                sığortalı mümkün tədbirləri görmüş olsaydı, zərərin məbləği həmin həcmdə azalmış olardı;
                <br>23.1.5. bu Qaydaların 23.2-ci bəndi nəzərə alınmaqla, sığortalının avtomobil, sığorta olunan və (və ya) sığorta hadisəsi barəsində
                sığortaçıya qəsdən yanlış məlumat verməsi nəticəsində sığortaçı sığorta riskini qiymətləndirmək, həmçinin sığorta hadisəsinin səbəblərini
                və (və ya) dəyən zərərin həcmini müəyyənləşdirmək imkanından tam və ya qismən məhrum olduqda;
                <br>23.1.6. avtomobilin sığortası üzrə sığortalı, sığorta olunan və ya faydalanan şəxs zərərin əvəzini tam olaraq zərər dəyməsində təqsirli
                olan şəxsdən aldıqda. Zərərvuran zərərin əvəzini qismən ödəmiş olduqda, sığorta ödənişindən ödənilmiş məbləğ həcmində imtina edilir;
                <br>23.1.7. baş vermiş hadisə sığorta müqaviləsinə görə sığorta hadisəsi hesab edilmədikdə;
                <br>23.1.8. sığorta müqaviləsində sığorta haqqının hissə-hissə ödənilməsinin razılaşdırıldığı və ilk hissəsinin ödənildiyi halda növbəti hər
                hansı hissəsinin müqavilədə nəzərdə tutulmuş ödənilməsi müddəti başa çatdıqdan 15 gün sonra, bu Qaydaların 14.3-cü bəndində nəzərdə
                tutulmuş halda isə sığortaçının müəyyən etdiyi müddətin başa çatmasından 3 gün sonra sığorta hadisəsinin baş verməsi halında sığorta
                haqqının müvafiq hissəsi ödənilməmiş olduqda.
                <br>23.2. Sığortaçı aşağıdakı hallarda sığorta ödənişinin verilməsindən imtina üçün müvafiq məlumatların verilməməsi, yaxud yanlış məlumatların verilməsi faktına əsaslana bilməz:
                <br>23.2.1. Sığorta müqaviləsi sığortalanma haqqında ərizə olmadan bağlandıqda;
                <br>23.2.2. sığortalanma haqqında ərizə sığortalı tərəfindən imzalanmadan tərtib edildikdə;
                <br>23.2.3. sığortalanma haqqında ərizə formasında nəzərdə tutulan hər hansı məlumatın və ya məlumatların həmin ərizədə
                göstərilmədiyi halda, sığorta müqaviləsi bağlanmış olduqda;
                <br>23.2.4. sığortalının sığortalanma haqqında ərizəyə əlavə edilən sənədlərdə yanlış məlumatların verilməsində təqsiri olmadıqda;
                <br>23.2.5. sığortalının sığortalanma haqqında ərizədə qəsdən yanlış məlumat verməsinin sığorta müqaviləsinin bağlandığı an
                sığortaçıya məlum olmasına baxmayaraq, sığorta müqaviləsi bağlanmış olduqda.
                <br>23.3. Bu Qaydaların 23.1.3-cü yarımbəndində müəyyən edilmiş hallarda sığortaçı zərərin həcmini müəyyənləşdirmək imkanından tam
                və ya qismən məhrum olduqda belə, aşkar faktlarla sübut olunan həcmdə sığorta ödənişindən imtina edə bilməz.
                <br>23.4. Azərbaycan Respublikası Mülki Məcəlləsində, Azərbaycan Respublikasının İnzibati Xətalar Məcəlləsində və Azərbaycan
                Respublikasının Cinayət Məcəlləsində nəzərdə tutulmuş məsuliyyəti istisna edən hallardan başqa, sığorta hadisəsi onun baş verməsinə
                yönələn, sığorta müqaviləsi üzrə sığortalı və (və ya) sığorta olunan hesab edilməyən faydalanan şəxsin qəsdən etdiyi hərəkəti və ya
                hərəkətsizliyi nəticəsində baş verdikdə, həmin faydalanan şəxs sığorta ödənişini almaq hüququndan məhrum olur.

            </p>
            <br>
            <h3><strong>24. Sığorta ödənişinin gecikdirilməsinin nəticəsi</strong></h3>
            <br>
            <p>
                Sığortaçı sığorta ödənişini bu Qaydalarla müəyyən olunmuş müddətdə ödəmədikdə hər gecikdirilmiş gün üçün sığorta ödənişi məbləğinin 0,1 faizi həcmində dəbbə pulu ödəyir
            </p>
            <br>
            <h3><strong>25. Subroqasiya hüququ</strong></h3>
            <br>
            <p>
                25.1. Sığortalanmış avtomobilə dəymiş zərərin əvəzini ödəmiş sığortaçı sığorta ödənişi almış şəxsin ona dəymiş zərərə görə məsuliyyət
                daşıyan şəxsə qarşı malik olduğu hüquqlardan və vasitələrdən istifadə edə bilər.
                <br>25.2. Faydalanan şəxsin zərərvuran şəxsə qarşı zərərin əvəzini ödəmək tələbi (iddiası) ilə bağlı hüququ sığorta ödənişini vermiş
                sığortaçıya subroqasiya qaydasında onun verdiyi sığorta ödənişi məbləğində keçir.
                <br>25.3. Faydalanan şəxs sığorta ödənişini aldıqda subroqasiya hüququnun həyata keçirilməsi üçün özündə olan bütün lazımi sənədlərlə
                sığortaçını təmin etməlidir.
                <br>25.4. Faydalanan şəxs zərərvuran şəxsə qarşı iddiadan və ya tələbi təmin edən hüquqlardan, yaxud lazımi sənədləri sığortaçıya
                verməkdən imtina etdikdə sığortaçı sığorta ödənişi verməkdən zərərvuran şəxsdən subroqasiya qaydasında ala biləcəyi məbləğ həcmində
                azad edilir.
                <br>25.5. Sığortaçı subroqasiya hüququndan zərərvuran şəxsin özünə və (və ya) müvafiq sığorta hadisəsi ilə bağlı risklər üzrə həmin şəxsin məsuliyyətini sığortalamış sığortaçıya, həmçinin dəyən zərərə görə sığortalı və ya faydalanan şəxs qarşısında maddi məsuliyyət daşıya bilən
                digər şəxsə qarşı istifadə edə bilər.

            </p>
            <br>
            <h3><strong>26. Mübahisələrin həlli qaydası</strong></h3>
            <br>
            <p>
                26.1. Sığorta müqaviləsinin icrasından irəli gələn mübahisələr tərəflərin qarşılıqlı razılığı ilə həll olunur.
                <br>26.2. Sığorta müqaviləsinin icrasından irəli gələn mübahisələrin həllində tərəflər arasında qarşılıqlı razılığın əldə olunması mümkün
                olmadıqda, mübahisələr məhkəmə qaydasında həll edilir.
                <br>26.3. Sığorta müqaviləsi üzrə hüquqlarının sığortaçı tərəfindən pozulduğunu hesab edən sığortalı, sığorta olunan və ya faydalanan
                şəxs sığorta nəzarəti orqanına (Azərbaycan Respublikasının Maliyyə Nazirliyinə) şikayət ərizəsi ilə müraciət edə bilər.
            </p>

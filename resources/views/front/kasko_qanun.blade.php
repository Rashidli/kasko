            <h3><strong>27. Sığortanın predmeti</strong></h3>
            <br>
            <p>
                Bu bölmə üzrə sığorta predmeti fiziki şəxslərin sahibliyində olan avtomobillərdir.

            </p>
            <br>
            <h3><strong>28. Sığorta riskləri</strong></h3>
            <br>
            <p>
                28.1. Sığortalıya bu Qaydalar əsasında aşağıdakı bəndlərin birində və ya bir neçəsində qeyd olunan risklər nəticəsində avtomobilə
                dəyə bilən zərərlərdən sığorta təminatı verilir:
                <br>28.1.1. <strong>avtonəqliyyat hadisəsi –</strong> avtomobilin istifadəsi nəticəsində baş verən, ölüm və bədən xəsarəti ilə nəticələnən, heyvanların və
                ya hərəkətsiz maneənin vurulmasına, habelə hər hansı digər əmlakın zədələnməsinə səbəb olan hadisələr;
                <br>28.1.2. <strong>atılmış və ya düşmüş əşya –</strong> avtomobilin hissələrinə zərər vurmadan rəng örtüyünün nöqtələr şəklində zədələnməsi halı
                istisna olmaqla, avtomobilin xarici səthinə daş, qar, buz və digər əşyaların atılması, dəyməsi və ya düşməsi;
                <br>28.1.3. <strong>üçüncü şəxsin qanunazidd hərəkəti;</strong>
                <br>28.1.4. <strong>oğurluq, quldurluq, qaçırma və soyğunçuluq;</strong>
                <br>28.1.5. <strong>yanğın və partlayış -</strong> <i>(a) yanğın</i> – alovlanma mənbəyindən asılı olmayaraq maddi ziyan vuran yanma, o cümlədən yüksək
                temperaturun, yanğın məhsullarının, eləcə də yanğının söndürülməsi üçün istifadə olunmuş su, köpük və digər vasitələrin təsiri, habelə
                yanğının birbaşa təsiri; <i>(b) partlayış</i> – qazların genişlənməyə meylliliyi nəticəsində böyük həcmdə enerjinin qısa müddət ərzində məhdud
                ölçülü məkandan sürətlə çıxaraq azad olması prosesi;
                <br>28.1.6. <strong>təbii fəlakət –</strong> sel, daşqın, zəlzələ, fırtına, qasırğa, tufan, dolu və digər təbii hadisələr;
                <br>28.1.7. <strong>heyvanların hərəkəti –</strong> avtomobilin salonuna vurulan zərərlər istisna olmaqla, heyvanların və quşların hərəkəti.
                <br>28.1.8. <strong>avtokasko –</strong> bu Qaydaların 28.1.1-28.1.7-ci yarımbəndlərində göstərilən risklərin məcmusu.

            </p>
            <br>
            <h3><strong>29. Sığorta hadisəsi</strong></h3>
            <br>
            <p>
                Sığorta müqaviləsinə görə sığorta ödənişinin sığortalıya, sığorta olunana və ya digər faydalanan şəxslərə ödənilməsi üçün əsas olan,
                sığorta müqaviləsinin qüvvədə olduğu müddət ərzində bu Qaydaların 28-ci bəndində nəzərdə tutulmuş risklərin baş verməsi nəticəsində
                sığortalanmış avtomobilə zərər dəyməsi faktı sığorta hadisəsi sayılır.
            </p>
            <br>
            <h3><strong>30. Sığorta təminatında əlavə məhdudiyyətlər</strong></h3>
            <br>
            <p>
                30.1. Sığorta müqaviləsində başqa cür nəzərdə tutulmursa, bu Qaydaların 17-ci bəndində göstərilənlərdən əlavə aşağıdakılara münasibətdə də sığorta təminatı verilmir:
                <br>30.1.1. aşağıdakı hallarda dəyən zərər:
                <br>30.1.1.1. avtomobilin dərman qutusunun, qəza dayanacağı işarəsinin, istehsalçı zavod tərəfindən quraşdırılmamış cihazlarının,
                həmçinin qeydiyyat nişanının oğurlanması, zədələnməsi və ya məhv olması;
                <br>30.1.1.2. avtomobilin digər hissələri də eyni hadisə nəticəsində zədələndiyi hallar istisna olmaqla, şinlərinin zədələnməsi;
                <br>30.1.1.3. avtomobilin açarlarına, elektron aktivləşdirmə kartına, brelok və s. bu kimi hissələrinə, onların avtomobildən kənarda
                olduqları halda zərər dəyməsi və ya bu cür halda onların oğurlanması;
                <br>30.1.1.4. avtomobilin banı və (və ya) hissələri zədələnmədən onun rəng örtüyünün qopması və ya digər hər hansı formada
                zədələnməsi;
                <br>30.1.1.5. avtomobildə heç kəsin olmadığı zaman qapılarının və ya baqajının açarla bağlı olmaması, pəncərələrinin və ya tavanının
                (lyukunun) açıq qalması, açarın nəzarətsiz buraxılması və ya avtomobilin içərisində qalması nəticəsində avtomobilin və ya onun içərisində
                yerləşən hissələrinin qeyd olunan müvafiq səbəblərdən istifadə edilməklə oğurlanması;
                <br>30.1.2. avtomobilin dəyərdən düşməsi;
                <br>30.1.3. avtomobilin vəziyyətini onun sığorta hadisəsindən əvvəlki vəziyyəti ilə müqayisədə yaxşılaşdıran hər hansı təmir və ya
                dəyişdirmə işləri;
                <br>30.1.4. avtomobilin bütövlükdə və ya onun ayrı-ayrı hissə və aqreqatlarının təbii aşınması, onun əmtəə görünüşünün itməsi, istehsal
                və təmir zamanı mövcud olan qüsurları;
                <br>30.1.5. sığortalının, sürücünün və ya sərnişinlərin sığorta hadisəsinin baş verdiyi anda avtomobildə olan əmlakına dəyən zərər.

            </p>
            <br>
            <h3><strong>31. Sığorta məbləği</strong></h3>
            <br>
            <p>
                31.1. Avtomobilin sığortası üzrə müəyyən olunan sığorta məbləği sığortalanan avtomobilin bazar dəyərindən artıq ola bilməz.
                <br>31.2. Avtomobilin sığorta müqaviləsi bağlanan andakı həqiqi, yəni sığorta dəyərindən artıq məbləğdə eyni risklərdən iki və ya daha
                çox sığortaçıda sığortalanması halında (ikili və ya çoxqat sığorta halında), sığorta hadisəsinin baş verdiyi zaman dəyən zərər üzrə
                sığortaçılardan hər biri onunla bağlanmış sığorta müqaviləsində nəzərdə tutulmuş sığorta məbləğinə mütənasib qaydada, ümumilikdə
                sığorta dəyərindən artıq olmamaq şərtilə öhdəlik daşıyır.
                <br>31.3. Avtomobil sığortalı tərəfindən qanunsuz gəlir əldə etmək məqsədilə bir və ya bir neçə sığorta müqaviləsi əsasında onun bazar
                dəyərindən artıq məbləğə sığorta etdirildikdə, bu məqsədlə bağlanmış hər bir sığorta müqaviləsi bağlandığı andan etibarsız sayılır. Bu
                halda sığortalının qanunsuz gəlir əldə etmək məqsədi məhkəmə qaydasında sübut edilməlidir.

            </p>
            <br>
            <h3><strong>32. Qismən sığorta</strong></h3>
            <br>
            <p>
                32.1. Avtomobilin sığortası üzrə müqavilədə müəyyən edilmiş sığorta məbləği sığorta dəyərindən az olduğu halda (qismən sığorta
                halında), sığortaçı zərərin əvəzini sığorta məbləğinin sığorta dəyərinə olan nisbətində ödəyir, bir şərtlə ki, qismən sığorta barədə müddəa
                sığorta müqaviləsində əks edilmiş olsun.
                <br>32.2. Sığorta hadisəsi nəticəsində qismən sığortalanmış avtomobilin tam məhv olması halında sığorta məbləği bütünlüklə
                ödənildikdə, sığorta predmetinin qalıqlarına olan hüquq sığortaçıya sığorta ödənişi məbləğinin sığorta dəyərinə olan nisbətində keçir.
                <br>32.3. Sığorta müqaviləsində “Ümumi sığorta məbləği” növü tətbiq edildiyi halda sığorta ödənişi verildikdən sonra belə sığorta
                təminatı qismən sığorta kimi qiymətləndirilə bilməz.


            </p>
            <br>
            <h3><strong>33. Sığorta ödənişinin həyata keçirilməsi</strong></h3>
            <br>
            <p>
                33.1. Sığorta ödənişinin məbləği sığorta hadisəsinin baş verməsi nəticəsində sığortalanmış avtomobilə dəyən real zərərin məbləğindən çox ola bilməz.
                <br>33.2. Sığortalanmış avtomobilə dəymiş zərərlə bağlı sığorta ödənişi sığortaçının seçimi əsasında aşağıdakı formalarda ödənilə bilər:
                <br>33.2.1. <strong>avtomobilə qismən zərər dəydikdə:</strong>
                <br>33.2.1.1. Zərərin məbləği barədə tərəflər arasında razılıq əldə edildikdən sonra zərər məbləğinin pul şəklində faydalanan şəxsə
                ödənilməsi;
                <br>33.2.1.2. zərərin aradan qaldırılması üçün sığortalıya göstərilən xidmətlərin və ya satılan əşyaların dəyərinin xidmət göstərənə və ya
                satıcıya ödənilməsi;
                <br>33.2.1.3. avtomobilin təmiri və ya bərpası;
                <br>33.2.2. avtomobilin <strong>tam məhv olması</strong>(avtomobilin sığorta hadisəsindən əvvəlki vəziyyətə çatdırılması üçün təmiri və ya bərpasına
                tələb olunan xərclərin avtomobilin bazar dəyərinin 75 %-ni və ya daha çox hissəsini təşkil etməsi), <strong>yaxud oğurlanması və ya qaçırılması</strong>
                halında:
                <br>33.2.2.1. sığorta məbləğindən çox olmamaq şərtilə avtomobilin sığorta hadisəsi baş verdiyi andan dərhal əvvəlki bazar dəyərinin pul
                şəklində ödənilməsi. Bu zaman sığortaçının avtomobilin qalıqlarına münasibətdə öz seçimi əsasında aşağıdakı hüquqları var:
                <br>33.2.2.1.1. avtomobilin qalıqlarının dəyərini sığorta ödənişi məbləğindən çıxmaq. Bu zaman sığortaçı ilə sığortalı arasında qalıqların
                dəyəri barədə razılıq əldə oluna bilmədikdə sığortaçı 15 iş günü ərzində qalıqların müəyyən etdiyi qiymətə satılmasını təmin edir və ya
                onları özü alır;
                <br>33.2.2.1.2. həmin qalıqların onun mülkiyyətinə verilməsini tələb etmək.
                <br>33.2.2.2. sığortalanmış avtomobilin eyni marka və modeldən olan, habelə eyni texniki vəziyyətdə olan digər avtomobillə əvəz edilməsi.
                <br>33.3. İpoteka qoyulan avtomobilin sığortası üzrə sığorta hadisəsi baş verdiyi tarixdə faydalanan şəxs olan ipoteka saxlayana borclunun
                (ipoteka qoyanın) borc (kredit) müqaviləsi üzrə sığorta ödənişinin verildiyi tarixə ona ödəməli olduğu məbləğdən artıq sığorta ödənişi verilə
                bilməz.
                <br>33.4. Girov qoyulan avtomobilin tam məhv olması ilə nəticələnən sığorta hadisəsi baş verdikdə, sığorta müqaviləsində nəzərdə
                tutulduğu halda faydalanan şəxs olan girov saxlayan borclunun (girov qoyanın) borc (kredit) müqaviləsi üzrə müəyyən edilmiş borcun
                ödənilməmiş hissəsində sığorta ödənişi almaq hüququ var.

            </p>
            <br>
            <h3><strong>34. Təmir və ya bərpa xərclərinin ödənilməsi zamanı nəzərə alınmayan amillər</strong></h3>
            <br>
            <p>
                34.0. Sığorta hadisəsi nəticəsində zərər dəymiş avtomobilin təmiri və ya bərpası xərcləri hesablanarkən aşağıdakılar nəzərə alınmır:
                <br>34.0.1. avtonəqliyyat vəsitəsinə texniki xidmət və onun zəmanətli təmiri;
                <br>34.0.2. avtomobilin ayrı-ayrı hissələrinin təbii aşınması və ya köhnəlməsi ilə bağlı aparılan bərpa işləri və ya belə hissələrin
                dəyişdirilməsi;
                <br>34.0.3. təmir yolu ilə istismara yararlı vəziyyətə gətirilə bilən hissələrin dəyişdirilməsi;
                <br>34.0.4. avtomobilin zədələnmiş hissəsinin rənglənməsindən əlavə onun zədələnməmiş digər hissələrinin rənglənməsi.
            </p>
            <br>

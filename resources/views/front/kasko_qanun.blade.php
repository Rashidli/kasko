            <br>
            <h3><strong>BÖLMƏ III. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası</strong></h3>
            <br>
            <br>
            <h3><strong>36. Sığortanın predmeti</strong></h3>
            <br>
            <p>
                Bu Bölmə üzrə sığorta predmeti bu Qaydaların 2.0.1-ci yarımbəndində nəzərdə tutulmuş avtomobillərin sahiblərinin mülki məsuliyyətinin yaranması halıdır.

            </p>
            <br>
            <h3><strong>37. Sığorta riskləri və sığorta hadisəsi</strong></h3>
            <br>
            <p>
                37.1. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə sığorta müqaviləsində göstərilən avtomobilin istifadəsi zamanı
                üçüncü şəxslərin həyatına, sağlamlığına və (və ya) əmlakına dəyən zərərin əvəzinin ödənilməsi üzrə sığorta olunanın mülki məsuliyyətinin
                yaranması riskindən sığorta təminatı verilir.
                <br>37.2. Sığorta ödənişinin ödənilməsi üçün əsas olan, sığorta müqaviləsinin qüvvədə olduğu müddət ərzində sığorta müqaviləsində
                göstərilən avtomobilin istifadəsi zamanı üçüncü şəxslərin həyatına, sağlamlığına və (və ya) əmlakına vurulan zərərin əvəzinin ödənilməsinə
                görə sığorta olunanın mülki məsuliyyəti üzrə öhdəliyinin bu Qaydaların 37.3-cü bəndi nəzərə alınmaqla yaranması faktı sığorta hadisəsi
                hesab edilir.
                <br>37.3. Sığorta müqaviləsinin qüvvədə olduğu müddət ərzində sığorta müqaviləsində göstərilən avtomobilin istifadəsi zamanı üçüncü
                şəxslərin əmlakına vurulan zərərin əvəzinin ödənilməsinə görə sığorta olunanın mülki məsuliyyəti üzrə öhdəliyinin “İcbari sığortalar
                haqqında” Azərbaycan Respublikası Qanununun 56.1.2-ci maddəsində nəzərdə tutulmuş müvafiq sığorta məbləğinə bərabər və ya ondan
                az olması faktı bu Qaydaların məqsədləri üçün sığorta hadisəsi sayılmır.
            </p>
            <br>
            <h3><strong>38. Sığorta təminatında əlavə məhdudiyyətlər</strong></h3>
            <br>
            <p>
                38.1. Sığorta müqaviləsində başqa cür nəzərdə tutulmursa, bu Qaydaların 17-ci bəndində göstərilənlərdən əlavə, aşağıdakılara münasibətdə də sığorta təminatı verilmir:
                <br>38.1.1. Sığortalının işçisinin avtomobilin sərnişini olarkən sığortalı ilə əmək münasibətlərindən irəli gələn peşə vəzifələrinin icrası
                zamanı ölməsi və ya bədən xəsarəti alması nəticəsində yaranan məsuliyyət;
                <br>38.1.2. avtomobilin sərnişini kimi hərəkət edən, sığorta olunanın hər hansı ailə üzvünün ölməsi və ya bədən xəsarəti alması
                nəticəsində yaranan məsuliyyət;
                <br>38.1.3. avtomobildə və (və ya) onun qoşqusunda olan yükün vurduğu zərərə görə məsuliyyət;
                <br>38.1.4. əmlaka vurulan zərərə görə əldən çıxmış fayda və ya digər dolayı zərərlərə görə məsuliyyət;
                <br>38.1.5. cərimələr və ya mənəvi zərərlərə görə təzminat ödəmək məsuliyyəti;
                <br>38.1.6. Üçüncü şəxslərin əmlakına dəyən zərər üzrə “İcbari sığortalar haqqında” Azərbaycan Respublikasının Qanununun 56.1.2 maddəsində nəzərdə tutulmuş təminat həddində ödənilməli olan zərər məbləğləri.

            </p>
            <br>
            <h3><strong>39. Sığorta məbləği və azadolma məbləği</strong></h3>
            <br>
            <p>
                39.1. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə sığorta məbləği tərəflərin razılığı ilə sığorta müqaviləsində müəyyən olunur və sığortaçının öhdəliyinin son həddini ifadə edir.
                <br>39.2. Üçüncü şəxsin əmlakına dəyən zərər üzrə azadolma məbləği “İcbari sığortalar haqqında” Azərbaycan Respublikası Qanununun
                56.1.2-ci maddəsində müəyyən edilmiş sığorta məbləği həcmində müəyyən edilir.
            </p>
            <br>
            <h3><strong>40. Sığorta ödənişinin verilməsi</strong></h3>
            <br>
            <p>
                40.1. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə sığorta hadisəsi baş verdikdə <strong>üçüncü şəxsin əmlakına dəyən
                birbaşa zərərin əvəzi</strong> aşağıdakı məbləğlərdə ödənilir:
                <br>40.1.1. əmlak tamamilə məhv olduqda – istifadə üçün yararlı olan qalıqların dəyəri çıxılmaq şərtilə, həmin əmlakın sığorta
                hadisəsinin baş verməsi anında mövcud olmuş bazar dəyəri məbləğində;
                <br>40.1.2. əmlak qismən zədələndikdə - həmin əmlakın sığorta hadisəsindən dərhal əvvəlki vəziyyətinə qaytarılması üçün tələb olunan
                xərc məbləğində.
                <br>40.2. Avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə sığorta hadisəsi baş verdikdə, <strong>üçüncü şəxsin həyat və
                sağlamlığına dəyən zərərin əvəzi</strong> aşağıdakı məbləğlərdə ödənilir:
                <br>40.2.1. zərərçəkənin malik olduğu, yaxud hadisə baş verməsəydi malik olması real olaraq ehtimal edilən və itirdiyi qazanc (gəlir) məbləğində;
                <br>40.2.2. zərərçəkənin sağlamlığının pozulması ilə bağlı çəkdiyi əlavə xərclərin, o cümlədən müalicəyə, əlavə qidalanmaya, dərman
                vasitələrinin alınmasına, protezləşdirməyə, başqasının qulluğuna, sanatoriya-kurort müalicəsinə, xüsusi nəqliyyat vasitələri əldə
                edilməsinə, başqa peşəyə hazırlanmaya çəkdiyi xərclər məbləğində, bu şərtlə ki, zərərçəkənin həmin yardım və qulluq növlərinə möhtac
                olduğu və bunları pulsuz almaq hüququnun qanunla nəzərdə tutulmadığı müəyyənləşdirilsin;
                <br>40.2.3. ailə başçısı olan zərərçəkənin öldüyü halda onun himayəsində olan şəxslərin itirdiyi qazanc məbləğində;
                <br>40.2.4. zərərçəkənin öldüyü halda 1000 (min) manatdan artıq olmamaq şərtilə onun dəfni ilə bağlı xərclər.
                <br>40.3. Sığorta hadisəsi nəticəsində üçüncü şəxsin həyatına və sağlamlığına dəyən zərərin əvəzi Azərbaycan Respublikasının Mülki
                Məcəlləsinin 1117-ci maddəsində müəyyən olunmuş qaydalar üzrə ödənilir.
                <br>40.4. Eyni sığorta hadisəsi nəticəsində bir neçə şəxsin əmlakına və ya həyat və sağlamlığına dəyən zərərin ümumi məbləği avtomobil
                sahiblərinin mülki məsuliyyətinin sığortası üzrə müəyyən olunmuş sığorta məbləğindən artıq olduqda sığortaçı hər bir zərərçəkənə ona
                vurulan zərər məbləğinə mütənasib məbləğdə sığorta ödənişi verir. Bu halda zərərçəkənlərin hamısına verilən sığorta ödənişinin ümümi məbləği avtomobil sahiblərinin mülki məsuliyyətinin sığortası üzrə müəyyən olunmuş sığorta məbləğinə bərabər olmalıdır.

            </p>
            <br>
            <h3><strong>BÖLMƏ IV. Sürücülərin və sərnişinlərin fərdi qəza sığortası</strong></h3>
            <br>
            <br>
            <h3><strong>41. Sığortanın predmeti</strong></h3>
            <br>
            <p>
                Bu Bölmə üzrə sığorta predmeti bu Qaydaların 2.0.1.-ci yarımbəndində nəzərdə tutulmuş avtomobillərin sürücüləri və sərnişinləridir.
            </p>
            <br>
            <h3><strong>42. Sığorta olunan</strong></h3>
            <br>
            <p>
                42.1. Sığorta müqaviləsində göstərilən avtomobildə olan sürücü və sərnişin sürücülərin və sərnişinlərin fərdi qəza sığortası üzrə
                sığorta olunan hesab olunurlar.
                <br>42.2. Sığorta olunanların sayı avtomobilin istehsalçısı tərəfindən nəzərdə tutulmuş oturacaq yerlərinin sayından çox ola bilməz
            </p>
            <br>
            <h3><strong>43. Sığorta riskləri</strong></h3>
            <br>
            <p>
                43.0. Fərdi qəza sığortası üzrə sığorta müqaviləsində göstərilən avtomobilin istifadəsi ilə əlaqədar sürücüsünün və (və ya) sərnişininin
                həyat və sağlamlığına dəyə bilən zərərin aşağıda göstərilən hallarla nəticələnməsi risklərindən sığorta təminatı verilir:
                <br>43.0.1. əmək qabilliyətinin itirilməsi ilə əlaqədar I, II, III dərəcə əlilliyin müəyyən olunması;
                <br>43.0.2. 18 yaşınadək uşağın sağlamlıq imkanlarının məhdudlaşması;
                <br>43.0.3. ölüm.
            </p>
            <br>
            <h3><strong>44. Sığorta hadisəsi və ona dair bəzi tələblər</strong></h3>
            <br>
            <p>
                44.1. Sığorta müqaviləsinin qüvvədə olduğu müddət ərzində və əhatə etdiyi ərazidə bu Qaydaların 28.1-ci bəndində müəyyən
                edilmiş risklərin hər hansı birinin baş verməsinin 43.1-ci bəndində nəzərdə tutulmuş hallarla nəticələnməsi, 44.2-ci və 44.3-cü bəndlərinin
                tələbləri nəzərə alınmaqla sığorta hadisəsi sayılır.
                <br>44.2. Sığorta olunanın ölməsi halı yalnız aşağıdakı halların hər biri mövcud olduqda sığorta hadisəsi sayılır:
                <br>44.2.1. ölümə səbəb olan bədbəxt hadisə sığorta müqaviləsi qüvvədə olan müddətdə baş vermiş olsun;
                <br>44.2.2. ölüm faktı ölümə birbaşa səbəb olan və sığorta müqaviləsinin qüvvədə olduğu müddətdə baş vermiş bədbəxt hadisənin
                nəticəsi olsun;
                <br>44.2.3. ölüm hadisəsi bədbəxt hadisənin baş verməsi tarixindən ən geci 1 il müddətində baş vermiş olsun.
                <br>44.3. Sığorta olunanın sağlamlığına dəymiş zərərlə əlaqədar olaraq ona I, II, III qrup əlilliyin təyin olunması və ya 18 yaşınadək
                uşağın sağlamlıq imkanlarının məhdudlaşması yalnız aşağıdakı halların hər biri mövcud olduqda sığorta hadisəsi sayılır:
                <br>44.3.1. zərərə səbəb olan bədbəxt hadisə sığorta müqaviləsi qüvvədə olan müddətdə baş vermiş olsun;
                <br>44.3.2. zərər birbaşa ona səbəb olmuş bədbəxt hadisə nəticəsində dəymiş olsun;
                <br>44.3.3. zərər bədbəxt hadisənin baş verməsi tarixindən ən geci 1 il müddətində müəyyən edilmiş olsun.

            </p>
            <br>
            <h3><strong>45. Sığorta təminatında əlavə məhdudiyyətlər</strong></h3>
            <br>
            <p>
                Bu Qaydaların 17-ci bəndində göstərilənlərdən əlavə, sığorta olunanın özünə sui qəsd etməsi halına da sığorta təminatı verilmir.
            </p>
            <br>
            <h3><strong>46. Sığorta məbləği</strong></h3>
            <br>
            <p>
                46.1. Sürücülərin və sərnişinlərin fərdi qəza sığortası müqaviləsi üzrə sığorta məbləği tərəflərin razılığı ilə sığorta müqaviləsində müəyyən olunur.
                <br>46.2. Sürücülərin və sərnişinlərin fərdi qəza sığortası üzrə sığorta məbləği avtomobilin bir oturacaq yeri, yəni bir sığorta olunan
                üçün sığorta məbləğini ifadə edir
            </p>
            <br>
            <h3><strong>47. Sığorta ödənişinin verilməsi</strong></h3>
            <br>
            <p>
                47.1. Sığorta hadisəsinin baş verməsi tarixindən 1 il müddətində s ığorta olunanın ölməsi halında sığorta ödənişi sığorta məbləğinin 100 faizi həcmində onun vərəsələrinə verilir.
                <br>47.2. Sığorta olunanın sağlamlığına zərər dəyməsi halında sığorta ödənişi aşağıdakı məbləğlərdə ödənilir:
                <br>47.2.1. əmək qabilliyətinin itirilməsi ilə əlaqədar:
                <br>47.2.1.1. Birinci dərəcə əlillik müəyyən olunduqda – sığorta məbləğinin 100 faizi;
                <br>47.2.1.2. ikinci dərəcə əlillik müəyyən olunduqda – sığorta məbləğinin 80 faizi;
                <br>47.2.1.3. üçüncü dərəcə əlillik müəyyən olunduqda – sığorta məbləğinin 60 faizi;
                <br>47.2.2. 18 yaşınadək uşağın sağlamlıq imkanlarının məhdudlaşması müəyyən olunduqda – sığorta məbləğinin 100 faizi.
                <br>47.3. Sığorta hadisəsi nəticəsində sığorta ödənişi almış sığorta olunana təyin edilmiş əlillik dərəcəsinin dəyişməsi, yaxud onun
                ölməsi ilə əlaqədar sığorta ödənişi növbəti dəfə verilərkən, həmin hadisə ilə bağlı bu Bölmə əsasında əvvəllər verilmiş sığorta ödənişi məbləğləri çıxılır.

            </p>

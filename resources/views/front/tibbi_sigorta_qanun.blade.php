            <h3 class="madde-item">Maddə 5. Tibbi sığorta şəhadətnaməsi </h3>
            <br>
            <p>
                Tibbi sığorta şəhadətnaməsi - sığorta olunana yaşayış yerindən asılı olmayaraq Azərbaycan
Respublikasının bütün ərazisində, habelə Azərbaycan Respublikası ilə tibbi sığorta sahəsində
müqavilə bağlayan digər dövlətin ərazisində tibbi sığorta proqramında göstərildiyindən az
olmayan həcmdə tibbi yardım almağa təminat verən sənəddir.
İcbari və könüllü tibbi sığorta müqaviləsinin subyekti olan vətəndaşlar tibbi sığorta
şəhadətnaməsi almalıdırlar.
Tibbi sığorta şəhadətnaməsi sığorta olunanın özündə olur və tibb müəssisəsinə müraciət
edərkən təqdim olunur.
Tibbi sığorta şəhadətnaməsinin forması və onun tətbiqi qaydaları müvafiq icra hakimiyyəti
orqanı tərəfindən təsdiq edilir.
            </p>
            <br>
            <h3 class="madde-item">Maddə 6. Sığortalının hüquq və vəzifələri </h3>
            <br>
            <p>
                sığortalının hüquqları aşağıdakılardır:
müvafiq icra hakimiyyəti orqanının müəyyən etdiyi qaydada könüllü tibbi sığorta müqaviləsi
bağlamaq və ya onu ləğv etmək;
könüllü tibbi sığorta təşkilatını sərbəst seçmək;
tibbi sığorta müqaviləsinin şərtlərinin yerinə yetirilməsinə nəzarət etmək.
sığortalının vəzifələri aşağıdakılardır:
müvafiq icra hakimiyyəti orqanının müəyyən etdiyi qaydada icbari tibbi sığorta müqaviləsi
bağlamaq;
icbari tibbi sığorta proqramına uyğun olaraq sığorta haqlarını vaxtında ödəmək;
sığorta olunanların sağlamlığının vəziyyətinin göstəriciləri haqqında tibbi sığorta təşkilatına
məlumat vermək;
könüllü tibbi sığorta müqaviləsi üzrə sığorta haqlarının tam məbləğdə və vaxtında ödənilməsi
barədə müvafiq icra hakimiyyəti orqanına məlumat vermək.
Tibbi sığorta müqaviləsində sığortalı üçün digər vəzifələr də nəzərdə tutula bilər.
            </p>

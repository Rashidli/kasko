            <h3 class="madde-item">
                Maddə 7. Tibbi sığorta sistemində sığorta olunan vətəndaşların hüquqları
            </h3>
            <br>
            <p>
                Tibbi sığorta sistemində sığorta olunan vətəndaşların hüquqları aşağıdakılardır:
tibbi sığorta şəhadətnaməsini aldıqları andan qanunvericilikdə müəyyən olunmuş
hüquqlardan istifadə etmək;
tibbi sığorta müqaviləsi ilə müəyyən edilən tibbi yardım növlərinə və həcminə uyğun olaraq
tibbi sığorta sisteminə daxil olan tibb müəssisələrindən hər hansı birini və həkimi seçmək;
profilaktik yardım da daxil olmaqla, Azərbaycan Respublikasının bütün ərazisində icbari
tibbi sığorta proqramındakından az olmayan həcmdə tibbi yardım almaq;
icbari tibbi sığorta müqaviləsinin şərtlərinə uyğun olan tibbi xidmətdən istifadə etmək;
tibbi sığorta müqaviləsinə əsasən müraciət etdiyi tibb müəssisəsindən sağlamlığının
vəziyyəti, eləcə də tətbiq olunan müalicə metodları və onların nəticələri barədə məlumat almaq;
sığortalı, tibbi sığorta təşkilatına, tibb müəssisəsinə, tibbi sığorta müqaviləsinin şərtlərindən
asılı olmayaraq, onların təqsiri üzündən vurulmuş zərərin ödənilməsi də daxil olmaqla,
qanunvericilikdə müəyyən olunmuş qaydada iddia qaldırmaq;
könüllü tibbi sığorta müqaviləsinin şərtlərində müəyyən edilmişsə, istifadə edilməyən sığorta
haqqının bir hissəsini geri almaq.
Vətəndaşların tibbi sığorta ilə bağlı mənafeyini müvafiq icra hakimiyyəti orqanı, tibbi sığorta
təşkilatları, həmkarlar ittifaqı təşkilatları və digər ictimai birliklər müdafiə edir.
Azərbaycan Respublikasından kənarda yaşayan Azərbaycan Respublikası vətəndaşlarının
tibbi sığortası Azərbaycan Respublikasının tərəfdar çıxdığı dövlətlərarası müqavilələrə əsasən
həyata keçirilir.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 8. Azərbaycan Respublikasında daimi yaşayan vətəndaşlığı olmayan şəxslərin və
əcnəbilərin tibbi sığortası
            </h3>
            <br>
            <p>
                Azərbaycan Respublikasında daimi yaşayan vətəndaşlığı olmayan şəxslər tibbi sığorta
sistemində Azərbaycan Respublikasının vətəndaşları ilə eyni hüquq və vəzifələrə malikdirlər.
Azərbaycan Respublikasında əcnəbilər tibbi sığorta sahəsində, Azərbaycan Respublikasının
tərəfdar çıxdığı dövlətlərarası müqavilələrdə başqa qaydalar nəzərdə tutulmayıbsa, Azərbaycan
Respublikasının vətəndaşları ilə eyni hüquq və vəzifələrə malikdirlər.
            </p>
            <br>
            <h3 class="madde-item">Maddə 9. İcbari tibbi sığorta sisteminin maliyyələşdirilməsi</h3>
            <br>
            <p>
                İcbari tibbi sığorta sistemi dövlət büdcəsindən, sığortalının (mülkiyyət formasından asılı
olmayaraq hüquqi şəxslərin və sahibkarlıq fəaliyyəti ilə məşğul olan fiziki şəxslərin) ödəmələri,
icbari tibbi sığorta haqlarının ödəmə qaydasının pozulması hallarında tətbiq olunan cərimə və
dəbbə pulu, qanunvericilikdə qadağan olunmayan digər mənbələr hesabına maliyyələşdirilir.
İcbari tibbi sığorta sisteminin maliyyələşdirilməsi, icbari tibbi sığorta haqlarının saxlanması
və maliyyə vəsaitinin məqsədli istifadəsinə nəzarət müvafiq icra hakimiyyəti orqanı tərəfindən
həyata keçirilir.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 10. Tibbi sığorta sistemində sığorta haqları
            </h3>
            <br>
            <p>
                İcbari tibbi sığorta sistemində sığortalılar işləyən vətəndaşlar üçün məbləği və ödənilmə
qaydası müvafiq icra hakimiyyəti orqanı tərəfindən müəyyən edilən icbari tibbi sığorta haqları
ödəyirlər. İcbari tibbi sığorta haqları icbari tibbi sığorta proqramının yerinə yetirilməsini təmin
edən məbləğdə müəyyən edilir.
İşləməyən pensiyaçılar, idarə və təşkilat ləğv olunduqda müvəqqəti işsizlər, əmək qabiliyyəti
olmayanlar, uşaqlar, tələbələr və büdcə təşkilatlarının işçiləri üçün icbari tibbi sığorta haqları
tibbi xidmətlər üçün qiymətlərin indeksasiyası nəzərə alınmaqla müvafiq icra hakimiyyəti orqanı
tərəfindən büdcə və sosial müdafiə fondlarının vəsaiti hesabına ödənilir.
Könüllü tibbi sığortada sığorta haqlarının məbləği könüllü tibbi sığorta proqramına uyğun
olaraq tərəflərin razılığı ilə müəyyən edilir.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 11. İcbari tibbi sığorta proqramı
            </h3>
            <br>
            <p>
                İcbari tibbi sığorta proqramı vətəndaşlara göstərilən tibbi xidmətin şərtlərini, növünü,
həcmini(baza zərfini) müəyyən edir və müvafiq icra hakimiyyəti orqanı tərəfindən təsdiq edilir.
Bu proqram vətəndaşların müəyyən olunmuş həcmdə və keyfiyyətdə tibbi yardım almasını
nəzərdə tutur.
            </p>
            <br>
            <h2>I I I f ə s i l
                <br>
                TİBBİ SIĞORTA TƏŞKİLATLARININ FƏALİYYƏTİ
            </h2>
            <br>
            <h3 class="madde-item">Maddə 12. Tibbi sığorta təşkilatı</h3>
            <br>
            <p>
                Könüllü tibbi sığorta təşkilatı – mülkiyyət formasından asılı olmayaraq, sığorta
                qanunvericiliyində müəyyən edilmiş tələblərə müvafiq qaydada xüsusi razılığa (lisenziyaya)
                malik olan və həmin qanunvericiliyə uyğun olaraq könüllü tibbi sığortanı həyata keçirmək
                səlahiyyəti olan sığortaçıdır.
                İcbari tibbi sığorta təşkilatı - icbari tibbi sığortanı həyata keçirmək üçün müvafiq icra
                hakimiyyəti orqanı tərəfindən yaradılan dövlət orqanıdır.
                Tibb müəssisələri tibbi sığorta təşkilatının təsisçiləri ola bilməzlər.
                Tibbi sığorta təşkilatından vergi tutulması Azərbaycan Respublikasının müvafiq
                qanunvericiliyinə uyğun olaraq tənzimlənir.
                Tibbi sığorta təşkilatının yaradılması, yenidən təşkili və ləğv olunması qanunvericilikdə
                müəyyən olunmuş qaydada həyata keçirilir.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 13. Tibbi sığorta təşkilatının hüquq və vəzifələri
            </h3>
            <br>
            <p>
                Tibbi sığorta təşkilatının hüquqları aşağıdakılardır:
tibbi sığorta müqavilələri üzrə tibbi yardım və xidmətlər göstərilməsi üçün tibb müəssisəsini
seçmək;
tibb müəssisələrinin akkreditasiyasında iştirak etmək;
tibbi xidmətlər üçün tariflərin müəyyənləşdirilməsində iştirak etmək;
tibb müəssisələri tərəfindən müqavilənin şərtlərinin pozulduğu hallarda tibbi xidmətlərə
çəkilən xərcləri qismən ödəmək;
tibb müəssisəsinin və (və ya) tibb işçisinin təqsiri üzündən sığorta olunana vurulmuş zərərin
ödənilməsi üçün qanunvericilikdə müəyyən edilmiş qaydada məhkəməyə müraciət etmək;
zərərin sığorta olunan tərəfindən vurulduğu hallar istisna olmaqla, vətəndaşın sağlamlığına
vurulmuş zərər üçün cavabdeh olan fiziki və ya hüquqi şəxslərdən qanunvericilikdə müəyyən
olunmuş qaydada sığorta olunana tibbi yardım göstərilməsinə sərf olunmuş xərclərin
ödənilməsini tələb etmək.
Tibbi sığorta təşkilatı sabit fəaliyyətini təmin etmək məqsədi ilə Azərbaycan Respublikası
qanunvericiliyində müəyyən olunmuş qaydada ehtiyat fondu yarada bilər.
Tibbi sığorta təşkilatının vəzifələri aşağıdakılardır:
tibbi sığorta olunanlara tibbi sığorta proqramına uyğun tibbi yardım göstərilməsi üçün
qanunvericilikdə müəyyənləşdirilmiş qaydada fəaliyyət göstərən tibb müəssisələri ilə
müqavilələr bağlamaq;
tibbi sığorta müqaviləsinin qüvvəyə mindiyi andan sığorta olunana tibbi sığorta
şəhadətnaməsini vermək;
tibb müəssisələrinə tibbi xidmətin haqqını müqavilədə nəzərdə tutulmuş qaydada və
müddətdə ödəmək;
könüllü tibbi sığorta müqaviləsində nəzərdə tutulmuşsa, sığorta haqlarının bir hissəsini
sığorta olunana qaytarmaq;
müqavilənin şərtlərinə uyğun olaraq tibbi yardımın həcminə, müddətinə və keyfiyyətinə
nəzarət etmək;
sığorta olunanın mənafelərini müdafiə etmək.
            </p>
            <br>
            <h2>I V f ə s i l
                <br>
                TİBBİ SIĞORTA SİSTEMİNDƏ TİBBİ MÜƏSSİSƏLƏRİNİN FƏALİYYƏTİ
            </h2>
            <br>
            <h3 class="madde-item">
                Maddə 14. Tibb müəssisələrinin özəl tibbi praktika ilə məşğul olan şəxslərin hüquq və
                vəzifələri
            </h3>
            <br>
            <p>
                Mülkiyyət formasından asılı olmayaraq qanunvericilikdə müəyyən olunmuş qaydada
fəaliyyət göstərən tibb müəssisələrinin və özəl tibbi praktika ilə məşğul olan şəxslərin tibbi
sığorta sistemində tibbi xidmətlər göstərmək hüququ vardır.
Özəl tibb müəssisələri və özəl tibbi praktika ilə məşğul olan şəxslər qanunvericiliyə uyğun
olaraq keçirilən müsabiqə əsasında icbari tibbi sığorta proqramlarının həyata keçirilməsində
iştirak edə bilərlər. Özəl tibb müəssisələri və özəl tibbi praktika ilə məşğul olan şəxslərlə
müqavilələr, onların icbari tibbi sığorta proqramlarında iştirakı hallarında, müvafiq icra
hakimiyyəti və yerli özünüidarəetmə orqanları, habelə tibbi sığorta təşkilatları ilə bağlanır.
Tibbi sığorta proqramlarını yerinə yetirən tibb müəssisələrinin tibbi sığorta sistemindən
kənarda da tibbi yardım göstərmək hüququ vardır.
Qanunvericilikdə müəyyən olunmuş qaydada tibbi sığorta proqramını yerinə yetirən tibb
müəssisələrinin və ya özəl tibbi praktika ilə məşğul olan könüllü şəxslərin tibbi sığorta təşkilatını
seçmək hüququ vardır.
Tibbi sığorta sistemində mülkiyyət formasından asılı olmayaraq tibb müəssisələri sığorta
olunanların əmək qabiliyyətinin müvəqqəti itirilməsini təsdiq edən sənəd vermək hüququna
malikdirlər.
İcbari tibbi sığorta sistemində mülkiyyət formasından asılı olmayaraq tibb müəssisələri və ya
özəl tibbi praktika ilə məşğul olan şəxslər tibbi sığorta proqramlarını yerinə yetirərkən:
sığorta olunana tibbi sığorta müqaviləsində nəzərdə tutulmuş müddətdə, həcmdə və
keyfiyyətdə tibbi yardım göstərməli;
sığorta olunanın xəstəliyi ilə əlaqədar baş verə biləcək, eyni zamanda sığorta olunanın
təqsirindən üzə çıxmış hər hansı fəsadlar barədə tibbi sığorta təşkilatına məlumat verməli;
sığorta olunanın sağlamlığının vəziyyətinə və ya şəxsi həyatına dair məlumatlar barədə
xidməti sirri qorumalıdırlar.
İcbari tibbi sığorta sistemində tibb müəssisələri və ya qanunvericilikdə müəyyən olunmuş
qaydada özəl tibbi praktika ilə məşğul olan şəxslər tibbi sığorta müqaviləsində nəzərdə tutulan
tibbi xidmətlərə görə sığorta olunandan əlavə haqq tələb edə bilməzlər.
Özəl tibb fəaliyyəti subyektlərinin icbari tibbi sığorta proqramlarında iştirakından asılı
olmayaraq, icbari tibbi sığorta qaydalarına əsasən təcili tibbi yardım göstərən özəl tibbi
fəaliyyətinin subyektlərinə ödəniş bu şəxslərin sığortalandığı tibbi sığorta təşkilatları tərəfindən
həyata keçirilir.

            </p>
            <br>
            <h3 class="madde-item">
                Maddə 15. Tibbi sığorta sistemində tibbi xidmətlər üçün tariflər
            </h3>
            <br>
            <p>
                İcbari tibbi sığorta sistemində tibbi xidmətlər üçün tariflər müvafiq icra hakimiyyəti orqanı
tərəfindən müəyyən edilir.
Tariflər tibb müəssisələrinin rentabelliyini və tibbi sığorta müqaviləsində nəzərdə tutulan
tibbi xidmətin müasir səviyyəsini təmin etməlidir.
Könüllü tibbi sığorta üzrə tibbi xidmətlər üçün tariflər sığorta təşkilatı ilə bu xidmətləri
göstərən tibb müəssisəsi (və ya özəl tibbi praktika ilə məşğul olan şəxs) arasında razılaşmaya
əsasən müəyyən edilir.
            </p>
            <br>
            <h2>V f ə s i l
                <br>
                YEKUN MÜDDƏALAR
            </h2>
            <br>
            <h3 class="madde-item">
                Maddə 16. Tibbi sığorta sistemində tərəflərin məsuliyyəti
            </h3>
            <br>
            <p>
                Mülkiyyət formasından asılı olmayaraq müəssisə, idarə və təşkilatların icbari tibbi sığorta
müqaviləsinin bağlanmasından imtina edilməsinə, sığorta haqlarının hesablanması üçün
məbləğlərin gizlədilməsinə və ya aşağı salınmasına, ödənilmə müddətlərinin pozulmasına görə
qanunvericilikdə müəyyən olunmuş qaydada müvafiq icra hakimiyyəti orqanı tərəfindən maliyyə
sanksiyaları tətbiq edilir. Cərimənin ödənilməsi hüquq pozuntusunun aradan qaldırılması
vəzifəsindən azad etmir.
Tibb müəssisəsi və ya özəl tibbi praktika ilə məşğul olan şəxs tibbi sığorta müqaviləsinin
şərtlərinə və qanunvericiliyin tələblərinə uyğun olaraq göstərilən tibbi xidmətin həcminə və
keyfiyyətinə görə, eləcə də sığorta olunana tibbi xidmət göstərməkdən imtina etməyə görə
məsuliyyət daşıyırlar.
Qanunvericilikdə müəyyən olunmuş qaydada fəaliyyət göstərən tibb müəssisələri və ya özəl
tibbi praktika ilə məşğul olan şəxslər tibbi sığorta müqaviləsinin şərtlərini pozduqda tibbi sığorta
təşkilatı göstərilən tibbi xidmətin haqqını tam və ya qismən ödəməmək hüququna malikdir.
Tibbi sığorta müqaviləsinin şərtlərinin yerinə yetirilməsinə görə tibbi sığorta təşkilatı sığorta
olunan və ya sığortalı qarşısında məsuliyyət daşıyır.
Tibbi sığorta sistemində mübahisələr Azərbaycan Respublikasının qanunvericiliyində
müəyyən edilmiş qaydada məhkəmə tərəfindən həll edilir.
            </p>

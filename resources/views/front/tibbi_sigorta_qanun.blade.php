            <h3 class="madde-item">Maddə 4. Tibbi sığorta müqaviləsi</h3>
            <br>
            <p>
                Tibbi sığorta onun subyektləri arasında bağlanmış müqavilələr formasında həyata keçirilir.
                Tibbi sığorta müqaviləsi - qanunvericilikdə müəyyən olunmuş qaydada sığortalı tibbi sığorta
                təşkilatları arasında bağlanan və sığorta olunana tibbi sığorta proqramına uyğun olaraq göstərilən
                tibbi yardımın maliyyələşdirilməsini və təşkilini müəyyən edən, sığorta olunanın digər
                mənafelərini müdafiə edən yazılı sazişdir.
                Tibbi sığorta müqaviləsi ilk sığorta haqqı ödənildiyi andan qüvvəyə minmiş hesab edilir.
                Tibbi sığorta müqaviləsinin forması və bağlanma qaydası müvafiq icra hakimiyyəti orqanı
                tərəfindən müəyyən edilir.
                Tibbi sığortanın ayrı-ayrı növlərinə aid qaydalar və icbari tibbi sığorta münasibətlərinin şamil
                edilmədiyi şəxslərin kateqoriyaları müvafiq icra hakimiyyəti orqanı tərəfindən müəyyən edilir.
            </p>

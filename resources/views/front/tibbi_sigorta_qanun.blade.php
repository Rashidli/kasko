                Tibbi sığorta sistemindəki tibb müəssisələrinə xüsusi razılığı olan müalicə-profilaktika, elmitədqiqat müəssisələri, eləcə də qanunvericilikdə müəyyən olunmuş qaydada tibbi yardım
                göstərən digər hüquqi şəxslər daxildirlər.
                Tibbi sığortanın subyektləri arasındakı mübahisələr qanunvericilikdə müəyyən olunmuş

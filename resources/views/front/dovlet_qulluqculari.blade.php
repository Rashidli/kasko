        <h2>Dövlət qulluqçularının icbari sığortası haqqında </h2>
        <h1>AZƏRBAYCAN RESPUBLİKASININ QANUNU</h1>
        <br>
        <p>
            Bu Qanun "Dövlət qulluğu haqqında" Azərbaycan Respublikasının Qanununa əsasən dövlət
            qulluqçularının sosial müdafiəsini təmin etmək məqsədi ilə onların icbari sığortası qaydalarını və
            şərtlərini müəyyən edir
        </p>
        <div class="sehife-inner">
            <h3 class="madde-item">Maddə 1. Dövlət qulluqçularının icbari sığortası haqqında qanunvericilik </h3>
            <br>
            <p>
                Dövlət qulluqçularının icbari sığortası bu Qanunla, Azərbaycan Respublikasının Mülki
Məcəlləsi, "Dövlət qulluğu haqqında", Sığorta fəaliyyəti haqqında Azərbaycan Respublikasının
qanunları ilə və digər normativ hüquqi aktlarla tənzimlənir.
            </p>
            <br>
            <h3 class="madde-item">Maddə 2. Sığorta olunanlar</h3>
            <br>
            <p>
                Bu Qanuna uyğun olaraq sığorta olunanlar "Dövlət qulluğu haqqında" Azərbaycan
Respublikasının Qanununda müəyyənləşdirilmiş vəzifələrin təsnifatına aid edilən dövlət
qulluqçulardır.
            </p>
            <br>
            <h3 class="madde-item">Maddə 3. Sığortaçı </h3>
            <br>
            <p>Dövlət qulluqçularının sığortaçısı dövlət sığorta orqanıdır. </p>
            <br>
            <h3 class="madde-item">Maddə 4. Sığorta məbləği və sığorta haqqı </h3>
            <br>
            <p>
                4.1. İnzibati vəzifələrin təsnifatından asılı olaraq sığorta məbləğləri aşağıda göstərilən
                miqdarda müəyyən olunur: <br>
                4.1.1. birinci təsnifat - altı min altı yüz manat;<br>
                4.1.2. ikinci və üçüncü təsnifat - beş min yüz yetmiş manat;<br>
                4.1.3. dördüncü, beşinci və altıncı təsnifat - üç min yeddi yüz qırx manat;<br>
                4.1.4. yeddinci, səkkizinci və doqquzuncu təsnifat - min doqquz yüz səksən manat;<br>
                (Çıxarılıb) <br>
                4.1.6. Yardımçı vəzifələr - min yüz manat. <br>
                4.2. Sığorta haqqı hər il üçün sığorta məbləğinin 0,3 faizi miqdarında müəyyən edilir.
            </p>
            <br>
            <h3 class="madde-item">Maddə 5. Sığorta hadisələri və ödənişləri </h3>
            <br>
            <p>
                5.1. Sığorta olunanın fərdi qəza nəticəsində həlak olması və sağlamlığına zərər vurulması
                (əlillik, sağlamlıq imkanlarının məhdudluğu və (və ya) xəsarət alması) sığorta hadisələri hesab
                edilir. <br>
                5.2. Sığorta hadisəsi baş verdikdə sığorta olunana bu Qanunun 4.1-ci maddəsində müəyyən
                edilmiş sığorta məbləğlərindən faizlə aşağıdakı sığorta ödənişləri verilir:<br>
                5.2.1. həlak olması -100 faiz;<br>
                5.2.2. aldığı xəsarət nəticəsində əlillik qrupu təyin olunması, 18 yaşınadək sağlamlıq
                imkanları məhdudluğunun müəyyən olunması:<br>
                I qrup, 18 yaşınadək sağlamlıq imkanlarının məhdudluğu - 80 faiz<br>
                II qrup - 60 faiz<br>
                III qrup - 40 faiz;<br>
                5.2.3. əlillik qrupu təyin olunmadan və ya 18 yaşınadək sağlamlıq imkanlarının məhdudluğu
                müəyyən olunmadan ağır xəsarət alması - 20 faiz;<br>
                5.2.4. yüngül xəsarət alması - 5 faiz<br>
                5.3. Sığorta olunan işdən çıxdıqda və ya başqa işə keçdikdə əvvəlki dövlət qulluğunda
                olarkən aldığı xəsarət nəticəsində 3 il ərzində vəfat etdikdə və ya sağlamlığına zərər vurulduqda
                ona bu Qanunun 5.2-ci maddəsinə uyğun olaraq sığorta ödənişi verilir.
            </p>
            <br>
            <h3 class="madde-item">Maddə 6. Sığortanın aparılması qaydası </h3>
            <br>
            <p>
                6.1. Dövlət qulluqçularının icbari sığortası sığortalılar (dövlət hakimiyyəti orqanları) üçün bu
                məqsədlə hər il dövlət büdcəsində nəzərdə tutulmuş vəsait hesabına aparılır. <br>
                6.2. sığortalılar növbəti ildə bu Qanunun 4.1-ci maddəsinə uyğun olaraq sığorta olunması
                nəzərdə tutulan dövlət qulluqçularının siyahısını tərtib edir və cari ilin sonunadək sığortaçıya
                təqdim edirlər. <br>
                6.3. sığortalılar dövlət qulluqçularının icbari sığortasını təmin etmək üçün onlara bu
                məqsədlə dövlət büdcəsindən ayrılan vəsaitdən tələb olunan sığorta haqlarının məbləğini hər il
                rüblər üzrə sığortaçıya ödəyirlər. <br>
                6.4. Sığorta haqlarının məbləği hər ilin əvvəlində dövlət qulluqçularının müəyyən olunmuş
                sayına, vəzifələrin təsnifatına uyğun olaraq hesablanır. <br>
                6.5. Sığortaçı hər rüb sığortalılara əvvəlki rübün icrasına dair hesabat təqdim edir. <br>
                6.6. Sığorta ödənişi verilmiş bir sığorta hadisəsinin nəticəsi olan əlillik qrupu, yaxud daha
                ağır əlillik qrupu təyin olunduqda, 18 yaşınadək sağlamlıq imkanlarının məhdudluğu müəyyən
                olunduqda və ya ölüm baş verdikdə, sığorta ödənişi verilərkən əvvəl ödənilmiş məbləğ çıxılır. <br>
                6.7. İcbari sığorta haqqında müqavilə sığortaçı və sığortalı arasında bağlanır.

            </p>
            <br>
            <h3 class="madde-item">Maddə 7. Sığorta ödənişinin verilməsi müddəti </h3>
            <br>
            <p>Sığortaçı bu Qanunda nəzərdə tutulmuş şərtlərlə və məbləğdə sığorta olunana və ya onun
                vərəsələrinə sığorta ödənişini müvafiq sənədləri aldığı gündən 7 təqvim günü ərzində verir.
                </p>
            <br>
            <h3 class="madde-item">Maddə 8. Hüquq və vəzifələr </h3>
            <br>
            <p>
                8.1. Sığortaçının və sığorta olunanın qarşılıqlı hüquq və vəzifələri sığorta qanunvericiliyinə
                uyğun olaraq müqavilədə müəyyən edilir.
                <br>8.2. Sığortaçı müqavilədə nəzərdə tutulmuş sığorta haqlarının məbləğini aldığı gündən bu
                Qanuna və müqaviləyə uyğun olaraq sığorta olunana sığorta ödənişini vermək öhdəliyini
                götürür.
                <br>8.3. Sığorta hadisəsi baş verdikdə sığortalı və ya sığorta olunan (və ya vərəsəsi) bu barədə
                sığortaçını xəbərdar etməlidir.
                <br>8.4. Sığorta olunanın və ya onun vərəsələrinin sığorta ödənişini alması üçün sığorta
                hadisəsinin baş verdiyi gündən 3 il keçənədək sığortaçıya və ya müvafiq dövlət hakimiyyəti
                orqanına müraciət etmək hüququ vardır.
            </p>
            <br>
            <h3 class="madde-item">Maddə 9. Sığorta ödənişlərinin alınması üçün tələb olunan sənədlər </h3>
            <br>
            <p>
                9.1. Sığorta ödənişlərinin alınması üçün sığorta olunan yaxud onun vərəsələri müəyyən
                olunmuş formada ərizə, şəxsiyyətini təsdiq edən sənəd və sığorta olunanın qulluq etdiyi və ya
                əvvəl qulluq etdiyi dövlət hakimiyyəti orqanında tutduğu vəzifə barədə arayışla birlikdə
                sığortaçıya aşağıdakı sənədləri təqdim edir:
                <br>9.1.1. həlak olması halında:
                sığorta olunanın ölümü haqqında şəhadətnamənin sürəti;
                vərəsəlik hüququ haqqında şəhadətnamənin notariat qaydasında təsdiq olunmuş surəti;
                <br>9.1.2. əlillik və ya 18 yaşınadək sağlamlıq imkanlarının məhdudluğu halında - əlilliyin təyin
                edilməsi və ya sağlamlıq imkanları məhdudluğunun müəyyən olunması haqqında (əlilliyin
                qrupu, əlilliyin və ya sağlamlıq imkanları məhdudluğunun səbəbi və təyin edilməsi tarixini
                göstərməklə) tibbi-sosial ekspert komissiyasının arayışının notariat qaydasında təsdiq edilmiş
                surəti;
                <br>9.1.3. xəsarət alma halında - ağır və ya yüngül xəsarətin alınması barədə tibbi arayış.
                <br>9.2. Ağır və yüngül xəsarət növlərinin siyahısını müvafiq icra hakimiyyəti orqanı müəyyən
                edir.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 10. Sığorta ödənişinin verilməsindən imtina
            </h3>
            <br>
            <p>
                10.0. Sığorta olunana və ya onun vərəsələrinə sığorta ödənişinin verilməsindən aşağıdakı
                hallarda imtina edilir:
                <br>10.0.1. sığorta olunanın alkoqol, narkotik vasitələrdən, psixotrop maddələrdən və ya toksiki
                maddələrdən istifadə etməsi nəticəsində sığorta hadisəsi baş verdiyi halda;
                <br>10.0.2. sığorta olunan özünə qəsd etdikdə və həmin hal məhkəmənin qanuni qüvvəyə minmiş
                qərarı ilə təsdiq olunduqda;
                <br>10.0.3. sığorta olunanın sığorta hadisəsi ilə birbaşa bağlı olan qəsdən cinayət törətdiyi barədə
                məhkəmənin qanuni qüvvəyə minmiş hökmü olduqda.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 11. Sığorta ehtiyatları
            </h3>
            <br>
            <p>
                Sığortaçı daxil olmuş sığorta haqlarından komisyon mükafatlarına 10 faizi ayırmaqla
                qanunvericiliklə müəyyən edilmiş qaydada sığorta ehtiyatlarını yaradır.

            </p>
            <br>
            <h3 class="madde-item">Maddə 12. Nəzarət və məsuliyyət
            </h3>
            <br>
            <p>
                12.1. Dövlət qulluqçularının icbari sığortasının vaxtında və düzgün aparılmasına sığortalılar
                və sığortaçı nəzarət edirlər.
                <br>12.2. Bu Qanunun müddəalarının pozulmasına görə sığortalıların, sığortaçının, onların
                bölmələrinin rəhbərləri və digər vəzifəli şəxsləri Azərbaycan Respublikasının qanunvericiliyinə
                uyğun olaraq məsuliyyət daşıyırlar.
            </p>
            <br>
            <h3 class="madde-item">
                Maddə 13. Mübahisələrə baxılma qaydası
            </h3>
            <br>
            <p>
                Dövlət qulluqçularının icbari sığortası ilə əlaqədar mübahisələr Azərbaycan Respublikasının
qanunvericiliyi ilə müəyyən edilmiş qaydada, o cümlədən məhkəmə qaydasında həll edilir.
            </p>
